//! Per-Worker composition root generation (v0.8 §4.5, v0.9 §5.1).
//!
//! Each Worker's `compose.ts` exports a `compose(env)` function that
//! assembles the context's deps and returns the surface the entry point
//! invokes — `on call` services for the internal Service Binding protocol
//! plus `on http` route wrappers for the external HTTP router.

use std::collections::HashMap;
use std::fmt::Write as _;

use crate::ast::*;
use crate::emitter::http_handler_method_name;
use crate::emitter::wrangler::{agent_binding_name, consumed_binding_name};
use crate::project::UnitTable;

pub fn emit_worker_compose(
    context: &str,
    table: &UnitTable,
    consumes: &[String],
    aliases: &HashMap<String, String>,
    unit_tables: &HashMap<String, UnitTable>,
    // v0.17: adapter unit name → its binding module path (relative to the out
    // root, `.js`). An adapter's external provider class lives in this module,
    // not in a `handlers.ts`.
    binding_modules: &HashMap<String, String>,
) -> String {
    let mut out = String::new();
    let _ = writeln!(out, "// Generated by karnc — do not edit by hand.");
    let _ = writeln!(out, "// composition root for `{context}` Worker.");
    writeln!(out).unwrap();
    // v0.15: cross-context capabilities this Worker uses (in handlers or in a
    // local provider's `given`) → `deps_key → consumed_context`. Their
    // providers are instantiated locally (model A1).
    let cross_caps = worker_cross_caps(table, consumes, aliases);

    let _ = writeln!(
        out,
        "import {{ type ServiceBinding }} from \"../../runtime.js\";"
    );
    let _ = writeln!(out, "import * as handlers from \"./handlers.js\";");
    // Import each consumed unit's provider classes. A consumed *context*'s
    // providers live in its Worker's `handlers.js`; a consumed *adapter*'s
    // external provider classes live in its binding module at the out root.
    let mut cross_ctxs: Vec<&String> = cross_caps.values().collect();
    cross_ctxs.sort();
    cross_ctxs.dedup();
    for cctx in &cross_ctxs {
        let ns = cctx.replace('.', "_");
        if let Some(module) = binding_modules.get(*cctx) {
            let _ = writeln!(out, "import * as {ns}__binding from \"../../{module}\";");
        } else {
            let dir = crate::project::worker_dir_name(cctx);
            let _ = writeln!(
                out,
                "import * as handlers_{ns} from \"../{dir}/handlers.js\";"
            );
        }
    }
    writeln!(out).unwrap();

    // Only consumed *contexts* become Service Bindings — a consumed adapter is
    // not a Worker (its capability is provided in-process via the binding).
    let mut sorted_consumes: Vec<&String> = consumes
        .iter()
        .filter(|t| !binding_modules.contains_key(*t))
        .collect();
    sorted_consumes.sort();

    // Env shape: one Service Binding per consumed context + DO bindings.
    let _ = writeln!(out, "export interface Env {{");
    for t in &sorted_consumes {
        let bind = consumed_binding_name(t);
        let _ = writeln!(out, "  {bind}: ServiceBinding;");
    }
    let mut agent_names: Vec<&String> = table.agents.keys().collect();
    agent_names.sort();
    for a in &agent_names {
        let bind = agent_binding_name(a);
        let _ = writeln!(out, "  {bind}: DurableObjectNamespace;");
    }
    let _ = writeln!(out, "}}");
    writeln!(out).unwrap();

    if !agent_names.is_empty() {
        let _ = writeln!(
            out,
            "type DurableObjectNamespace = {{ idFromName(name: string): {{ toString(): string }}; get(id: any): any }};"
        );
        writeln!(out).unwrap();
    }

    let _ = writeln!(out, "export function compose(env: Env) {{");

    // v0.15: instantiate consumed-context capability providers locally first,
    // so local providers (and handlers) can depend on them by their deps key.
    let mut cross_keys: Vec<&String> = cross_caps.keys().collect();
    cross_keys.sort();
    for key in &cross_keys {
        let cctx = &cross_caps[*key];
        let ns = cctx.replace('.', "_");
        let provider_ts = unit_tables
            .get(cctx)
            .and_then(|t| t.providers.get(*key))
            .map(|p| p.provider_name.name.clone())
            .unwrap_or_else(|| (*key).clone());
        if binding_modules.contains_key(cctx) {
            // External (adapter) provider: instantiate from the binding module.
            let _ = writeln!(out, "  const {key} = new {ns}__binding.{provider_ts}();");
        } else {
            let _ = writeln!(out, "  const {key} = new handlers_{ns}.{provider_ts}();");
        }
    }

    // Capabilities: instantiate each capability's provider. v0.12: providers
    // are emitted in dependency order (a composed provider's `given` deps must
    // exist first) as local `const` bindings, injecting each provider's deps;
    // then assembled into the `deps` object. v0.15: a provider's `given` may
    // include cross-context capability keys (instantiated above).
    let order = crate::emitter::topo_order_providers(&table.providers);
    for cap in &order {
        let provider = table.providers.get(cap).unwrap();
        let provider_ts = &provider.provider_name.name;
        if provider.given.is_empty() {
            let _ = writeln!(out, "  const {cap} = new handlers.{provider_ts}();");
        } else {
            let dep_obj = provider
                .given
                .iter()
                .map(|c| c.key().to_string())
                .collect::<Vec<_>>()
                .join(", ");
            let _ = writeln!(
                out,
                "  const {cap} = new handlers.{provider_ts}({{ {dep_obj} }});"
            );
        }
    }
    let mut deps_entries: Vec<String> = {
        let mut caps: Vec<String> = order.clone();
        caps.extend(cross_keys.iter().map(|k| (*k).clone()));
        caps.sort();
        caps
    };
    // env passes through so handlers' cross-context calls (Service Bindings)
    // and agent instantiations (Durable Object namespaces) can reach it.
    if !sorted_consumes.is_empty() || !table.agents.is_empty() {
        deps_entries.push("env".to_string());
    }
    let _ = writeln!(out, "  const deps = {{ {} }};", deps_entries.join(", "));

    // Local-surface object: one async wrapper per service operation plus
    // one wrapper per `on http` handler.
    let mut service_names: Vec<&String> = table.services.keys().collect();
    service_names.sort();
    let _ = writeln!(out, "  return {{");
    for sname in &service_names {
        let service = table.services.get(*sname).unwrap();
        let mut cron_idx = 0usize;
        let mut queue_idx = 0usize;
        for h in &service.handlers {
            match &h.kind {
                HandlerKind::Call => {
                    emit_call_wrapper(&mut out, sname, h);
                }
                HandlerKind::Http { method, path } => {
                    emit_http_wrapper(&mut out, sname, h, *method, path);
                }
                HandlerKind::Cron { .. } => {
                    emit_cron_wrapper(&mut out, sname, cron_idx, h);
                    cron_idx += 1;
                }
                HandlerKind::Queue { .. } => {
                    emit_queue_wrapper(&mut out, sname, queue_idx, h);
                    queue_idx += 1;
                }
            }
        }
    }
    let _ = writeln!(out, "  }};");

    let _ = writeln!(out, "}}");
    out
}

/// v0.15: cross-context capabilities this Worker references in handlers or in
/// a local provider's `given`, as `deps_key → consumed_context`.
fn worker_cross_caps(
    table: &UnitTable,
    consumes: &[String],
    aliases: &HashMap<String, String>,
) -> std::collections::BTreeMap<String, String> {
    fn resolve(
        prefix: &str,
        consumes: &[String],
        aliases: &HashMap<String, String>,
    ) -> Option<String> {
        if let Some(q) = aliases.get(prefix) {
            return Some(q.clone());
        }
        if consumes.iter().any(|c| c == prefix) {
            return Some(prefix.to_string());
        }
        None
    }
    let mut out = std::collections::BTreeMap::new();
    let mut givens: Vec<&[CapRef]> = Vec::new();
    for s in table.services.values() {
        for h in &s.handlers {
            givens.push(&h.given);
        }
    }
    for a in table.agents.values() {
        for h in &a.handlers {
            givens.push(&h.given);
        }
    }
    for p in table.providers.values() {
        givens.push(&p.given);
    }
    for given in givens {
        for c in given {
            if let Some(p) = c.prefix()
                && let Some(ctx) = resolve(&p, consumes, aliases)
            {
                out.entry(c.key().to_string()).or_insert(ctx);
            }
        }
    }
    out
}

fn emit_call_wrapper(out: &mut String, sname: &str, h: &Handler) {
    let param_decls: Vec<String> = h
        .params
        .iter()
        .map(|p| format!("{}: any", p.name.name))
        .collect();
    let param_args: Vec<String> = h.params.iter().map(|p| p.name.name.clone()).collect();
    let _ = writeln!(out, "    async {sname}({}) {{", param_decls.join(", "));
    let _ = writeln!(
        out,
        "      return handlers.{sname}.call({}{}deps);",
        param_args.join(", "),
        if param_args.is_empty() { "" } else { ", " },
    );
    let _ = writeln!(out, "    }},");
}

fn emit_cron_wrapper(out: &mut String, sname: &str, cron_idx: usize, h: &Handler) {
    let method_key = crate::emitter::cron_handler_method_name(sname, cron_idx);
    // A cron handler takes an optional scheduled-time parameter; forward it (if
    // any) to the bound handler, with deps trailing.
    let param_decls: Vec<String> = h
        .params
        .iter()
        .map(|p| format!("{}: any", p.name.name))
        .collect();
    let param_args: Vec<String> = h.params.iter().map(|p| p.name.name.clone()).collect();
    let _ = writeln!(out, "    async {method_key}({}) {{", param_decls.join(", "));
    let _ = writeln!(
        out,
        "      return handlers.{sname}.{method_key}({}{}deps);",
        param_args.join(", "),
        if param_args.is_empty() { "" } else { ", " },
    );
    let _ = writeln!(out, "    }},");
}

fn emit_queue_wrapper(out: &mut String, sname: &str, queue_idx: usize, h: &Handler) {
    let method_key = crate::emitter::queue_handler_method_name(sname, queue_idx);
    // The queue handler takes its message parameter; forward it with deps.
    let param_decls: Vec<String> = h
        .params
        .iter()
        .map(|p| format!("{}: any", p.name.name))
        .collect();
    let param_args: Vec<String> = h.params.iter().map(|p| p.name.name.clone()).collect();
    let _ = writeln!(out, "    async {method_key}({}) {{", param_decls.join(", "));
    let _ = writeln!(
        out,
        "      return handlers.{sname}.{method_key}({}{}deps);",
        param_args.join(", "),
        if param_args.is_empty() { "" } else { ", " },
    );
    let _ = writeln!(out, "    }},");
}

fn emit_http_wrapper(out: &mut String, sname: &str, h: &Handler, method: HttpMethod, path: &str) {
    let method_key = http_handler_method_name(method, path);
    let param_decls: Vec<String> = h
        .params
        .iter()
        .map(|p| format!("{}: any", p.name.name))
        .collect();
    let param_args: Vec<String> = h.params.iter().map(|p| p.name.name.clone()).collect();
    let _ = writeln!(out, "    async {method_key}({}) {{", param_decls.join(", "));
    let _ = writeln!(
        out,
        "      return handlers.{sname}.{method_key}({}{}deps);",
        param_args.join(", "),
        if param_args.is_empty() { "" } else { ", " },
    );
    let _ = writeln!(out, "    }},");
}
