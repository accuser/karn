fn emit_capability(out: &mut String, c: &CapabilityDecl) {
    emit_doc_block(out, c.documentation.as_deref(), 0);
    writeln!(out, "export interface {name} {{", name = c.name.name).unwrap();
    for op in &c.ops {
        emit_doc_block(out, op.documentation.as_deref(), INDENT_STEP);
        let params: Vec<String> = op
            .params
            .iter()
            .map(|p| format!("{}: {}", p.name.name, ts_type_ref(&p.type_ref)))
            .collect();
        writeln!(
            out,
            "  {name}({params}): {ret};",
            name = op.name.name,
            params = params.join(", "),
            ret = ts_type_ref(&op.return_type),
        )
        .unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
    // Injection token (symbol carrying the interface type).
    writeln!(
        out,
        "export const {name}Token: unique symbol = Symbol(\"{name}\");",
        name = c.name.name
    )
    .unwrap();
    writeln!(out).unwrap();
}

fn emit_provider(out: &mut String, p: &ProviderDecl, commons: &TypedCommons, ctx: &EmitProjectCtx) {
    emit_doc_block(out, p.documentation.as_deref(), 0);
    writeln!(
        out,
        "export class {prov} implements {cap} {{",
        prov = p.provider_name.name,
        cap = p.capability.name,
    )
    .unwrap();
    for op in &p.ops {
        let params: Vec<String> = op
            .params
            .iter()
            .map(|p| format!("{}: {}", p.name.name, ts_type_ref(&p.type_ref)))
            .collect();
        let async_kw = if is_effectful_return(&op.return_type) {
            "async "
        } else {
            ""
        };
        writeln!(
            out,
            "  {async_kw}{name}({params}): {ret} {{",
            name = op.name.name,
            params = params.join(", "),
            ret = ts_type_ref(&op.return_type),
        )
        .unwrap();
        let mut cx = LowerCtx::new(commons, &ctx.cross_context);
        cx.local_agents = ctx.local_agents.clone();
        let async_tail = is_effectful_return(&op.return_type);
        emit_block_as_function_body(out, &op.body, &mut cx, INDENT_STEP * 2, async_tail);
        writeln!(out, "  }}").unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "export const {prov}Provider = {{ token: {cap}Token, factory: () => new {prov}() }};",
        prov = p.provider_name.name,
        cap = p.capability.name,
    )
    .unwrap();
    writeln!(out).unwrap();
}

fn emit_service(out: &mut String, s: &ServiceDecl, commons: &TypedCommons, ctx: &EmitProjectCtx) {
    emit_doc_block(out, s.documentation.as_deref(), 0);
    writeln!(out, "export const {name} = {{", name = s.name.name).unwrap();
    for handler in &s.handlers {
        emit_doc_block(out, handler.documentation.as_deref(), INDENT_STEP);
        let kind_name = match &handler.kind {
            HandlerKind::Call => "call".to_string(),
            HandlerKind::Http { method, path } => http_handler_method_name(*method, path),
        };
        // For service handlers the operation name is the handler kind
        // (e.g. `call`). v0.5 has only one handler kind, so the service is a
        // single-operation object literal.
        let mut params: Vec<String> = handler
            .params
            .iter()
            .map(|p| format!("{}: {}", p.name.name, ts_type_ref(&p.type_ref)))
            .collect();
        // Lower the body first so we can detect cross-context usage and
        // adjust the deps shape accordingly.
        let mut body_out = String::new();
        let mut cx = LowerCtx::new(commons, &ctx.cross_context);
        cx.capabilities = handler
            .given
            .iter()
            .map(|c| c.name.clone())
            .collect::<HashSet<_>>();
        cx.local_agents = ctx.local_agents.clone();
        cx.target = ctx.target;
        let async_tail = is_effectful_return(&handler.return_type);
        emit_block_as_function_body(
            &mut body_out,
            &handler.body,
            &mut cx,
            INDENT_STEP * 2,
            async_tail,
        );
        // Append the deps parameter (may include surface field if the body
        // made cross-context calls).
        let deps_ty =
            build_deps_object_ty_with_surface(&handler.given, &cx, &ctx.cross_context, ctx.target);
        params.push(format!("deps: {deps_ty}"));
        let ret = ts_type_ref(&handler.return_type);
        let async_kw = if is_effectful_return(&handler.return_type) {
            "async "
        } else {
            ""
        };
        writeln!(
            out,
            "  {async_kw}{op}({params}): {ret} {{",
            op = kind_name,
            params = params.join(", "),
        )
        .unwrap();
        out.push_str(&body_out);
        writeln!(out, "  }},").unwrap();
    }
    writeln!(out, "}};").unwrap();
    writeln!(out).unwrap();
}

fn build_deps_object_ty_with_surface(
    given: &[Ident],
    cx: &LowerCtx<'_>,
    cross_context: &crate::resolver::CrossContextInfo,
    target: BuildTarget,
) -> String {
    let mut parts: Vec<String> = given
        .iter()
        .map(|c| format!("{}: {}", c.name, c.name))
        .collect();
    match target {
        BuildTarget::Bundle => {
            if cx.cross_context_used {
                parts.push(format!("surface: {}", surface_ty(cross_context)));
            }
        }
        BuildTarget::Workers => {
            // v0.9.2: in workers mode `env` carries both consumed-context
            // Service Bindings and the local agents' Durable Object namespaces.
            // It is threaded into deps whenever the handler makes a
            // cross-context call or instantiates an agent.
            if cx.cross_context_used || cx.agents_instantiated {
                let agents = if cx.agents_instantiated {
                    sorted_local_agents(cx)
                } else {
                    Vec::new()
                };
                parts.push(format!("env: {}", workers_env_ty(cross_context, &agents)));
            }
        }
    }
    if parts.is_empty() {
        return "{}".to_string();
    }
    format!("{{ {} }}", parts.join("; "))
}

/// Local agent names in this commons, sorted — the DO bindings `env` exposes
/// in workers mode.
fn sorted_local_agents(cx: &LowerCtx<'_>) -> Vec<String> {
    let mut names: Vec<String> = cx
        .commons
        .commons
        .items
        .iter()
        .filter_map(|i| match i {
            CommonsItem::Agent(a) => Some(a.name.name.clone()),
            _ => None,
        })
        .collect();
    names.sort();
    names
}

/// Workers-mode deps.env shape: one Service Binding per consumed context and
/// one Durable Object namespace per local agent.
fn workers_env_ty(cross_context: &crate::resolver::CrossContextInfo, agents: &[String]) -> String {
    let mut consumed_sorted = cross_context.consumed_contexts.clone();
    consumed_sorted.sort();
    let mut entries: Vec<String> = consumed_sorted
        .iter()
        .map(|q| {
            let bind = crate::emitter::wrangler::consumed_binding_name(q);
            format!("{bind}: ServiceBinding")
        })
        .collect();
    for agent in agents {
        let bind = crate::emitter::wrangler::agent_binding_name(agent);
        entries.push(format!("{bind}: DurableObjectNamespace"));
    }
    if entries.is_empty() {
        "{}".to_string()
    } else {
        format!("{{ {} }}", entries.join("; "))
    }
}

/// Build the TS type for the `surface` field in deps, naming each consumed
/// context by its surface key plus the consumed context's makeSurface type.
fn surface_ty(cross_context: &crate::resolver::CrossContextInfo) -> String {
    let mut entries: Vec<(String, String)> = Vec::new();
    // Use alias if present, else the last segment of the qualified name.
    // Order: stable (sorted) so the diff is deterministic.
    let mut consumed_sorted = cross_context.consumed_contexts.clone();
    consumed_sorted.sort();
    // Reverse lookup: consumed-context qualified name → alias.
    let mut alias_for: HashMap<String, String> = HashMap::new();
    for (alias, target) in &cross_context.aliases {
        alias_for.insert(target.clone(), alias.clone());
    }
    for q in &consumed_sorted {
        let key = alias_for
            .get(q)
            .cloned()
            .unwrap_or_else(|| q.rsplit('.').next().unwrap_or(q.as_str()).to_string());
        let ns = qualified_to_ns(q);
        entries.push((key, format!("ReturnType<typeof {ns}.makeSurface>")));
    }
    if entries.is_empty() {
        return "{}".to_string();
    }
    let body = entries
        .into_iter()
        .map(|(k, v)| format!("{k}: {v}"))
        .collect::<Vec<_>>()
        .join("; ");
    format!("{{ {body} }}")
}

/// Turn a qualified context name (e.g. `commerce.payment`) into the JS
/// namespace ident used in `import * as <ns>` (`commerce_payment`).
fn qualified_to_ns(q: &str) -> String {
    q.replace('.', "_")
}

/// The PascalCase name a context uses for its generated `Deps` interface:
/// `shortener.links` → `ShortenerLinks`.
fn context_pascal(name: &str) -> String {
    name.split('.')
        .map(|seg| {
            let mut chars = seg.chars();
            match chars.next() {
                Some(c) => c.to_uppercase().collect::<String>() + chars.as_str(),
                None => String::new(),
            }
        })
        .collect()
}

/// Emit the per-context `<Ctx>Deps` interface (v0.9.2 §4): the providers the
/// context contributes plus the surfaces of any consumed contexts. Replaces
/// the fragile `Parameters<typeof svc.call>[1]` indexing, which only resolved
/// correctly for single-argument service operations.
fn emit_context_deps_interface(
    out: &mut String,
    commons: &TypedCommons,
    ctx: &EmitProjectCtx,
) -> String {
    let deps_name = format!("{}Deps", context_pascal(&commons.commons.name.joined()));
    let mut fields: Vec<String> = commons
        .commons
        .items
        .iter()
        .filter_map(|i| match i {
            CommonsItem::Capability(c) => Some(format!("  readonly {n}: {n};", n = c.name.name)),
            _ => None,
        })
        .collect();
    if !ctx.cross_context.consumed_contexts.is_empty() {
        fields.push(format!(
            "  readonly surface: {};",
            surface_ty(&ctx.cross_context)
        ));
    }
    writeln!(out, "export interface {deps_name} {{").unwrap();
    for f in &fields {
        writeln!(out, "{f}").unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
    deps_name
}

/// Emit the `makeSurface(deps)` function for a context that exposes
/// services to other contexts (v0.6 §6.3 / §6.4).
fn emit_make_surface(out: &mut String, commons: &TypedCommons, ctx: &EmitProjectCtx) {
    let services: Vec<&ServiceDecl> = commons
        .commons
        .items
        .iter()
        .filter_map(|i| match i {
            CommonsItem::Service(s) => Some(s),
            _ => None,
        })
        .collect();
    if services.is_empty() {
        return;
    }
    let deps_name = emit_context_deps_interface(out, commons, ctx);
    writeln!(out, "export function makeSurface(deps: {deps_name}) {{").unwrap();
    writeln!(out, "  return {{").unwrap();
    for s in &services {
        // For each handler kind currently only `call`. We bind it as a
        // method on the surface with the deps captured.
        let handler = s
            .handlers
            .iter()
            .find(|h| matches!(h.kind, HandlerKind::Call));
        let Some(h) = handler else { continue };
        let async_kw = if is_effectful_return(&h.return_type) {
            "async "
        } else {
            ""
        };
        let param_decls: Vec<String> = h
            .params
            .iter()
            .map(|p| format!("{}: {}", p.name.name, ts_type_ref(&p.type_ref)))
            .collect();
        let param_args: Vec<String> = h.params.iter().map(|p| p.name.name.clone()).collect();
        let ret = ts_type_ref(&h.return_type);
        writeln!(
            out,
            "    {async_kw}{sname}({params}): {ret} {{",
            sname = s.name.name,
            params = param_decls.join(", "),
        )
        .unwrap();
        writeln!(
            out,
            "      return {svc}.call({args}{sep}deps);",
            svc = s.name.name,
            args = param_args.join(", "),
            sep = if param_args.is_empty() { "" } else { ", " },
        )
        .unwrap();
        writeln!(out, "    }},").unwrap();
    }
    writeln!(out, "  }};").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
}

/// Lower a cross-context call in workers mode to a `callService(...)`
/// invocation. The argument types are looked up in the consumed context's
/// service signature so we know which serialise/deserialise helpers to
/// reference.
fn lower_workers_cross_context_call(
    consumed: &str,
    method: &Ident,
    args: &[Expr],
    stmts: &mut Vec<String>,
    cx: &mut LowerCtx<'_>,
) -> String {
    let info = cx.cross_context;
    let consumed_ns = qualified_to_ns(consumed);
    let binding = crate::emitter::wrangler::consumed_binding_name(consumed);

    // Look up the service signature on the consumed context.
    let svc = info
        .consumed_services
        .get(consumed)
        .and_then(|map| map.get(&method.name));

    // Build serialised-arg expression. If we know the parameter types, use
    // the owning context's serialise_<T> helper; otherwise fall back to
    // `value as JsonValue`.
    let mut args_serialised: Vec<String> = Vec::new();
    for (i, a) in args.iter().enumerate() {
        let lowered = lower_expr(a, stmts, cx);
        let param_ty = svc.and_then(|s| s.params.get(i)).map(|(_, t)| t);
        let serialised = match param_ty {
            Some(tr) => workers_serialise_expr(tr, &lowered, &consumed_ns),
            None => format!("{lowered} as JsonValue"),
        };
        args_serialised.push(serialised);
    }
    let args_json = if args_serialised.len() == 1 {
        args_serialised.into_iter().next().unwrap()
    } else {
        // Multi-arg: wrap into an object literal keyed by parameter names.
        let pairs: Vec<String> = svc
            .map(|s| {
                s.params
                    .iter()
                    .enumerate()
                    .map(|(i, (name, _))| {
                        let serialised = args_serialised
                            .get(i)
                            .cloned()
                            .unwrap_or_else(|| "null".to_string());
                        format!("{name}: {serialised}")
                    })
                    .collect()
            })
            .unwrap_or_else(|| {
                args_serialised
                    .iter()
                    .cloned()
                    .enumerate()
                    .map(|(i, s)| format!("arg{i}: {s}"))
                    .collect()
            });
        format!("{{ {} }}", pairs.join(", "))
    };

    // Deserialise the return value via the consumed context's helper.
    let deser_ref = match svc {
        Some(s) => workers_deserialise_ref(&s.return_type, &consumed_ns),
        None => "((j: any) => ({ tag: \"Ok\", value: j }))".to_string(),
    };

    format!(
        "callService(deps.env.{binding}, \"{}\", {args_json}, {deser_ref})",
        method.name
    )
}

fn workers_serialise_expr(tr: &TypeRef, value: &str, owning_ns: &str) -> String {
    match tr {
        TypeRef::Base(_, _) => format!("{value} as JsonValue"),
        TypeRef::Named(id) => format!("{owning_ns}.serialise_{}({value})", id.name),
        TypeRef::Result(_, _, _) | TypeRef::Option(_, _) => {
            let inst = workers_inner_ts_name(tr);
            format!("{owning_ns}.serialise_{inst}({value})")
        }
        TypeRef::Effect(inner, _) => workers_serialise_expr(inner, value, owning_ns),
        _ => format!("{value} as JsonValue"),
    }
}

fn workers_deserialise_ref(tr: &TypeRef, owning_ns: &str) -> String {
    // Strip the Effect wrapper — the caller already awaits the Promise.
    let inner = match tr {
        TypeRef::Effect(t, _) => t.as_ref(),
        other => other,
    };
    match inner {
        TypeRef::Named(id) => format!("{owning_ns}.deserialise_{}", id.name),
        TypeRef::Result(_, _, _) | TypeRef::Option(_, _) => {
            let inst = workers_inner_ts_name(inner);
            format!("{owning_ns}.deserialise_{inst}")
        }
        _ => "((j: any) => ({ tag: \"Ok\", value: j }))".to_string(),
    }
}

fn workers_inner_ts_name(t: &TypeRef) -> String {
    match t {
        TypeRef::Base(b, _) => b.name().to_string(),
        TypeRef::Named(id) => id.name.clone(),
        TypeRef::Result(a, b, _) => format!(
            "Result_{}_{}",
            workers_inner_ts_name(a),
            workers_inner_ts_name(b)
        ),
        TypeRef::Option(a, _) => format!("Option_{}", workers_inner_ts_name(a)),
        TypeRef::Effect(a, _) => format!("Effect_{}", workers_inner_ts_name(a)),
        TypeRef::HttpResult(a, _) => format!("HttpResult_{}", workers_inner_ts_name(a)),
        TypeRef::ValidationError(_) => "ValidationError".to_string(),
        TypeRef::Unit(_) => "Unit".to_string(),
    }
}

/// If `receiver` is a dotted chain or single ident that matches one of the
/// current context's `consumes` clauses (by alias or qualified name), return
/// the consumed context's qualified name plus the surface key used to access
/// it through `deps.surface.<key>`.
fn cross_context_lowering_prefix(receiver: &Expr, cx: &LowerCtx<'_>) -> Option<(String, String)> {
    let chain = flatten_emit_ident_chain(receiver)?;
    let info = cx.cross_context;
    if info.consumed_contexts.is_empty() && info.aliases.is_empty() {
        return None;
    }
    let consumed = info.resolve_prefix(&chain)?;
    // Surface key: prefer the alias if there is one, else the last segment.
    let mut alias_for: HashMap<String, String> = HashMap::new();
    for (alias, target) in &info.aliases {
        alias_for.insert(target.clone(), alias.clone());
    }
    let key = alias_for.get(&consumed).cloned().unwrap_or_else(|| {
        consumed
            .rsplit('.')
            .next()
            .unwrap_or(consumed.as_str())
            .to_string()
    });
    Some((consumed, key))
}

fn flatten_emit_ident_chain(e: &Expr) -> Option<String> {
    match &e.kind {
        ExprKind::Ident(id) => Some(id.name.clone()),
        ExprKind::FieldAccess { receiver, field } => {
            let head = flatten_emit_ident_chain(receiver)?;
            Some(format!("{head}.{}", field.name))
        }
        _ => None,
    }
}

/// Cast an argument crossing a context boundary to the consumed context's
/// type. For named types we emit `arg as <ns>.<TypeName>`. For other types
/// (base, ()), no cast is needed. The structural compatibility check at the
/// karn layer guarantees the cast is sound.
fn param_cast(
    consumed: &str,
    info: &crate::resolver::CrossContextInfo,
    method: &Ident,
    idx: usize,
    arg: String,
) -> String {
    let Some(svcs) = info.consumed_services.get(consumed) else {
        return arg;
    };
    let Some(service) = svcs.get(&method.name) else {
        return arg;
    };
    let Some((_, ptype_ref)) = service.params.get(idx) else {
        return arg;
    };
    if let Some(name) = type_ref_named_root(ptype_ref) {
        let ns = qualified_to_ns(consumed);
        // v0.9.1: when both contexts brand the same commons type (e.g., both
        // see `Money` with their own `__ctxBrand`), a direct
        // `as <ns>.<Type>` cast is rejected by `tsc --strict` because the
        // brand discriminants are incompatible. Karn guarantees the value's
        // base type matches at the boundary, so route through `unknown` to
        // tell TypeScript to trust the structural Karn-side check.
        return format!("({arg} as unknown as {ns}.{name})");
    }
    arg
}

/// If the type-ref names a single user type at its root, return that name.
/// (For generics like `Result[T, E]`, we don't emit a cast at the outer
/// layer — TypeScript handles the variance through the intersection.)
fn type_ref_named_root(r: &TypeRef) -> Option<&str> {
    match r {
        TypeRef::Named(id) => Some(id.name.as_str()),
        _ => None,
    }
}

#[allow(dead_code)]
fn build_deps_object_ty(given: &[Ident]) -> String {
    if given.is_empty() {
        return "{}".to_string();
    }
    let parts: Vec<String> = given
        .iter()
        .map(|c| format!("{}: {}", c.name, c.name))
        .collect();
    format!("{{ {} }}", parts.join("; "))
}

fn emit_agent(out: &mut String, a: &AgentDecl, commons: &TypedCommons, ctx: &EmitProjectCtx) {
    emit_doc_block(out, a.documentation.as_deref(), 0);
    let state_ty = format!("{}State", a.name.name);
    // 1) State record type.
    writeln!(out, "export interface {state_ty} {{").unwrap();
    for f in &a.state_fields {
        writeln!(
            out,
            "  readonly {name}: {ty};",
            name = f.name.name,
            ty = ts_type_ref(&f.type_ref),
        )
        .unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
    // v0.9.2: per-agent state registry (bundle mode + `karnc test`) and the
    // zero-value factory used to initialise a fresh key's state.
    let registry = agent_registry_name(&a.name.name);
    let zero_fn = format!("__zeroOf{}State", a.name.name);
    writeln!(out, "const {registry} = new StateRegistry();").unwrap();
    let zero_record = crate::checker::agent_state_zero_record(&a.state_fields, &commons.types)
        .unwrap_or_else(|| "{}".to_string());
    writeln!(
        out,
        "function {zero_fn}(): {state_ty} {{ return {zero_record}; }}"
    )
    .unwrap();
    writeln!(out).unwrap();
    // 2) Durable Object class.
    writeln!(out, "export class {name} {{", name = a.name.name).unwrap();
    writeln!(out, "  state: DurableObjectState;").unwrap();
    writeln!(out, "  constructor(state: DurableObjectState) {{").unwrap();
    writeln!(out, "    this.state = state;").unwrap();
    writeln!(out, "  }}").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "  private async loadState(): Promise<{state_ty}> {{").unwrap();
    writeln!(
        out,
        "    const stored = await this.state.storage.get<{state_ty}>(\"state\");"
    )
    .unwrap();
    writeln!(out, "    return stored ?? {zero_fn}();").unwrap();
    writeln!(out, "  }}").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "  private async commitState(s: {state_ty}): Promise<void> {{"
    )
    .unwrap();
    writeln!(out, "    await this.state.storage.put(\"state\", s);").unwrap();
    writeln!(out, "  }}").unwrap();
    writeln!(out).unwrap();
    // 3) Handlers.
    for h in &a.handlers {
        emit_doc_block(out, h.documentation.as_deref(), INDENT_STEP);
        let mut params: Vec<String> = h
            .params
            .iter()
            .map(|p| format!("{}: {}", p.name.name, ts_type_ref(&p.type_ref)))
            .collect();
        // Lower body into a buffer so we can detect cross-context usage and
        // shape the deps type accordingly.
        let mut body_out = String::new();
        let mut cx = LowerCtx::new(commons, &ctx.cross_context);
        cx.in_agent_handler = true;
        cx.agent_state_var = Some("currentState".to_string());
        cx.agent_key_field = Some(a.key_name.name.clone());
        cx.capabilities = h
            .given
            .iter()
            .map(|c| c.name.clone())
            .collect::<HashSet<_>>();
        cx.local_agents = ctx.local_agents.clone();
        let async_tail = is_effectful_return(&h.return_type);
        emit_block_as_function_body(&mut body_out, &h.body, &mut cx, INDENT_STEP * 2, async_tail);
        let deps_ty =
            build_deps_object_ty_with_surface(&h.given, &cx, &ctx.cross_context, ctx.target);
        params.push(format!("deps: {deps_ty}"));
        let ret = ts_type_ref(&h.return_type);
        let async_kw = if is_effectful_return(&h.return_type) {
            "async "
        } else {
            ""
        };
        let method = h
            .method_name
            .as_ref()
            .map(|m| m.name.clone())
            .unwrap_or_else(|| match &h.kind {
                HandlerKind::Call => "call".to_string(),
                HandlerKind::Http { .. } => "call".to_string(),
            });
        writeln!(
            out,
            "  {async_kw}{method}({params}): {ret} {{",
            params = params.join(", "),
        )
        .unwrap();
        // Load state at entry so the body's references to `self.state` work.
        // (We bind a local `currentState` for the body and provide `self`
        // through ID substitution at lowering time.)
        writeln!(out, "    const currentState = await this.loadState();").unwrap();
        out.push_str(&body_out);
        writeln!(out, "  }}").unwrap();
        writeln!(out).unwrap();
    }
    // v0.9.2: workers-mode DO dispatch. Method calls arrive as `fetch` requests
    // under `/_karn/agent/<method>`; decode `{ args, deps }`, invoke the
    // handler with deps as the trailing argument, and serialise the result.
    if matches!(ctx.target, BuildTarget::Workers) {
        writeln!(out, "  async fetch(request: Request): Promise<Response> {{").unwrap();
        writeln!(out, "    const url = new URL(request.url);").unwrap();
        writeln!(
            out,
            "    if (url.pathname.startsWith(\"/_karn/agent/\")) {{"
        )
        .unwrap();
        writeln!(
            out,
            "      const methodName = url.pathname.slice(\"/_karn/agent/\".length);"
        )
        .unwrap();
        writeln!(
            out,
            "      const {{ args, deps }} = (await request.json()) as {{ args: unknown[]; deps: unknown }};"
        )
        .unwrap();
        writeln!(
            out,
            "      const result = await (this as any)[methodName](...args, deps);"
        )
        .unwrap();
        writeln!(
            out,
            "      return new Response(JSON.stringify(result), {{ headers: {{ \"content-type\": \"application/json\" }} }});"
        )
        .unwrap();
        writeln!(out, "    }}").unwrap();
        writeln!(
            out,
            "    return new Response(\"Not Found\", {{ status: 404 }});"
        )
        .unwrap();
        writeln!(out, "  }}").unwrap();
        writeln!(out).unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
    // v0.9.2: agent-construction factory. Lowering of `AgentName(key)` calls
    // this. A present DO binding (workers) routes through `makeWorkersAgent`;
    // otherwise the bundle registry path is taken. The single `makeAgent`
    // helper keeps the call site target-agnostic.
    let key_ts = ts_type_ref(&a.key_type);
    let bind = crate::emitter::wrangler::agent_binding_name(&a.name.name);
    writeln!(
        out,
        "export function {factory}(key: {key_ts}, env?: {{ {bind}?: DurableObjectNamespace }}): {agent} {{",
        factory = agent_factory_name(&a.name.name),
        agent = a.name.name,
    )
    .unwrap();
    writeln!(
        out,
        "  return makeAgent({registry}, env?.{bind}, key, (state) => new {agent}(state));",
        agent = a.name.name,
    )
    .unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
}

/// The module-level state-registry constant name for an agent class.
fn agent_registry_name(agent: &str) -> String {
    format!("__{agent}Registry")
}

/// The exported agent-construction factory name for an agent class.
pub fn agent_factory_name(agent: &str) -> String {
    format!("__make{agent}")
}

/// Per-function lowering context: fresh-temp counter + typed-commons handle
/// (used to look up receiver types for method-call UFCS lowering).
struct LowerCtx<'a> {
    next_tmp: u32,
    commons: &'a TypedCommons,
    /// Names of capabilities in scope as `given C1, C2, ...`. Used to lower
    /// `Capability.op(args)` calls to `deps.Capability.op(args)`.
    capabilities: HashSet<String>,
    /// True when lowering an agent handler body. Used to rewrite `self.state`
    /// and `self.<keyField>` access into the appropriate locals.
    in_agent_handler: bool,
    /// The local variable holding the loaded state inside an agent handler.
    agent_state_var: Option<String>,
    /// The name of the agent's `key id` field (so `self.<id>` resolves).
    agent_key_field: Option<String>,
    /// Cross-context info for v0.6 cross-context call lowering.
    cross_context: &'a crate::resolver::CrossContextInfo,
    /// True if the current handler made at least one cross-context call
    /// (drives whether `deps` gets a `surface` field type).
    cross_context_used: bool,
    /// v0.7: when lowering a test case body, the target context's local
    /// service names. A `service.call(args)` or `service(args)` invocation
    /// where `service` is in this set lowers to `<service>.call(args, deps)`
    /// so the test wires its `deps` through.
    pub test_services: HashSet<String>,
    /// v0.7: when lowering a test case body, the target context's local
    /// agent names. `<Agent>(<key>).method(args)` lowers to
    /// `new Agent(makeTestState(...)).method(args, {})`.
    pub test_agents: HashSet<String>,
    /// Agent names declared in the surrounding context. Drives lowering of
    /// `Agent(key)` (to `new Agent(makeTestState(String(key)))`) and of
    /// `agent_instance.method(args)` (to `instance.method(args, deps)`) in
    /// service and agent-handler bodies. Populated by the caller for non-test
    /// emission and from `test_agents` in test emission.
    pub local_agents: HashSet<String>,
    /// Variable bindings that point at agent instances. Updated by the
    /// statement emitter when it sees `let x = AgentName(key)`. Used by
    /// the method-call lowering so `x.method(args)` resolves through
    /// the agent's class rather than via the receiver-namespace lookup.
    pub local_agent_vars: HashMap<String, String>,
    /// v0.8 build target. In workers mode cross-context calls lower to
    /// `callService(...)` instead of `deps.surface.<key>.<method>(...)`.
    pub target: BuildTarget,
    /// v0.9.2: set when the body instantiates a local agent. In workers mode
    /// this drives `env` (carrying the DO namespaces) into the handler's deps
    /// type so the agent factory can reach its Durable Object binding.
    pub agents_instantiated: bool,
    /// When an `is` receiver is not a simple, repeatable lvalue (e.g. a call
    /// like `parse(x) is Ok(n)`), it is evaluated once into a temp; the temp
    /// name is cached here keyed by the receiver expression's span so the
    /// `.tag` check and every pattern binding reference the *same* single
    /// evaluation. Simple receivers (idents / field chains) are never cached
    /// and continue to be rendered inline as before.
    is_receiver_temps: HashMap<crate::span::Span, String>,
}

impl<'a> LowerCtx<'a> {
    fn new(
        commons: &'a TypedCommons,
        cross_context: &'a crate::resolver::CrossContextInfo,
    ) -> Self {
        Self {
            next_tmp: 0,
            commons,
            capabilities: HashSet::new(),
            in_agent_handler: false,
            agent_state_var: None,
            agent_key_field: None,
            cross_context,
            cross_context_used: false,
            test_services: HashSet::new(),
            test_agents: HashSet::new(),
            local_agents: HashSet::new(),
            local_agent_vars: HashMap::new(),
            target: BuildTarget::Bundle,
            agents_instantiated: false,
            is_receiver_temps: HashMap::new(),
        }
    }
    /// v0.9.2: lower an agent instantiation `AgentName(key)` to its factory
    /// call. Bundle/test mode passes only the key; workers mode also threads
    /// `deps.env` so the factory can reach the agent's DO namespace.
    fn agent_construct(&mut self, agent: &str, key_expr: &str) -> String {
        self.agents_instantiated = true;
        let factory = agent_factory_name(agent);
        if matches!(self.target, BuildTarget::Workers) {
            format!("{factory}({key_expr}, deps.env)")
        } else {
            format!("{factory}({key_expr})")
        }
    }
    fn fresh(&mut self) -> String {
        let n = self.next_tmp;
        self.next_tmp += 1;
        format!("__r{n}")
    }
    /// Return a stable textual reference to an `is` receiver, used by the
    /// `.tag` check in `lower_is`. A simple, repeatable lvalue is lowered
    /// inline exactly as before (preserving rewrites such as `self.state` or
    /// capability access). A complex receiver (anything `value_text_for_is`
    /// could not render — e.g. a call) is evaluated once into a fresh temp
    /// emitted into `stmts` and cached by span, so the bindings gathered later
    /// reference the same evaluation rather than re-running the expression.
    fn is_receiver_ref(&mut self, value: &Expr, stmts: &mut Vec<String>) -> String {
        if let Some(t) = self.is_receiver_temps.get(&value.span) {
            return t.clone();
        }
        let lowered = lower_expr(value, stmts, self);
        if is_simple_is_receiver(value) {
            return lowered;
        }
        let tmp = self.fresh();
        stmts.push(format!("const {tmp} = {lowered};"));
        self.is_receiver_temps.insert(value.span, tmp.clone());
        tmp
    }
    /// Read-only counterpart for the binding gatherer (which has no `stmts`
    /// and cannot lift). If the receiver was already lifted to a temp during
    /// condition lowering, reuse that temp; otherwise it must be a simple
    /// repeatable lvalue, rendered inline. The "lower the condition before
    /// gathering its bindings" ordering in `emit_if_tail` / `lower_and_with_is`
    /// guarantees the temp exists before this is called for complex receivers.
    fn is_receiver_text(&self, value: &Expr) -> String {
        if let Some(t) = self.is_receiver_temps.get(&value.span) {
            return t.clone();
        }
        value_text_for_is(value)
    }
    fn receiver_namespace(&self, e: &Expr) -> Option<String> {
        let ty = self.commons.expr_types.get(&e.span)?;
        if let Ty::Named { name, .. } = ty {
            Some(name.clone())
        } else {
            None
        }
    }
    /// Resolve the payload field name for the i-th positional binding of
    /// a variant. Built-ins are recognised by name; user variants are
    /// looked up via the type tables.
    fn positional_field_name(
        &self,
        discriminant_ty: Option<&Ty>,
        variant: &str,
        idx: usize,
    ) -> String {
        match (variant, idx) {
            ("Ok", 0) | ("Some", 0) => return "value".to_string(),
            ("Err", 0) => return "error".to_string(),
            _ => {}
        }
        if let Some(Ty::Named {
            kind: NamedKind::Sum,
            name,
        }) = discriminant_ty
            && let Some(decl) = self.commons.types.get(name)
            && let TypeBody::Sum(s) = &decl.body
            && let Some(v) = s.variants.iter().find(|v| v.name.name == variant)
            && let Some(f) = v.payload.get(idx)
        {
            return f.name.name.clone();
        }
        // Single-field fallback. The checker rejects mixed bindings already.
        "value".to_string()
    }
}

/// Lower a block to a sequence of TypeScript statements suitable for use as
/// an async function body. Used by v0.7 mock-operation emission.
pub fn lower_block_to_async_body(
    block: &Block,
    return_type: &TypeRef,
    typed: &mut TypedCommons,
    cross_context: &crate::resolver::CrossContextInfo,
) -> String {
    let mut out = String::new();
    let mut cx = LowerCtx::new(typed, cross_context);
    let async_tail = is_effectful_return(return_type);
    emit_block_as_function_body(&mut out, block, &mut cx, 0, async_tail);
    out
}

/// Lower a test-case body: statements followed by a discarded tail expression
/// (the runner records success via the assertion mechanism, not via a return
/// value). Used by v0.7 test emission.
pub fn lower_test_case_body(
    block: &Block,
    typed: &mut TypedCommons,
    cross_context: &crate::resolver::CrossContextInfo,
    test_services: HashSet<String>,
    test_agents: HashSet<String>,
) -> String {
    let mut out = String::new();
    let mut cx = LowerCtx::new(typed, cross_context);
    cx.test_services = test_services;
    cx.local_agents = test_agents.clone();
    cx.test_agents = test_agents;
    for stmt in &block.statements {
        emit_statement(&mut out, stmt, &mut cx, 0);
    }
    // Evaluate the tail expression but discard its value; assertions inside
    // it still take effect via thrown AssertionErrors.
    let mut stmts = Vec::new();
    let tail = lower_expr(&block.tail, &mut stmts, &mut cx);
    for s in &stmts {
        write_line(&mut out, 0, s);
    }
    if !tail.is_empty() && tail != "undefined" {
        write_line(&mut out, 0, &format!("void ({tail});"));
    }
    out
}

fn emit_block_as_function_body(
    out: &mut String,
    block: &Block,
    cx: &mut LowerCtx,
    indent: usize,
    async_tail: bool,
) {
    for stmt in &block.statements {
        emit_statement(out, stmt, cx, indent);
    }
    // Tail position: match → inline switch, if → inline if, otherwise return expr.
    match &block.tail.kind {
        ExprKind::Match { discriminant, arms } => {
            emit_match_tail(out, discriminant, arms, cx, indent, async_tail);
        }
        ExprKind::If {
            cond,
            then_block,
            else_block,
        } if !both_simple(then_block, else_block) || cond_has_is_bindings(cond) => {
            emit_if_tail(out, cond, then_block, else_block, cx, indent, async_tail);
        }
        _ => {
            let mut stmts = Vec::new();
            let tail = lower_tail_expr(&block.tail, &mut stmts, cx, async_tail);
            for s in &stmts {
                write_line(out, indent, s);
            }
            write_line(out, indent, &format!("return {tail};"));
        }
    }
}

/// Lower an expression that's in the tail position of a returning context.
///
/// In async-tail position (v0.7.1), an `async function` wraps its return value
/// as a Promise automatically, so `Effect.pure(...)` is redundant and should
/// emit as a bare value. Recurse through control-flow forms whose result is
/// the surrounding function's return value:
/// - `Effect.pure(x)` → lower `x` directly.
/// - A ternary-form `if`/`else` (simple branches) where each branch's tail is
///   itself an async-tail position.
/// - A pure-tail block (no statements) where the inner tail is the actual
///   returned expression.
/// - Parens (transparent).
///
/// In non-async-tail position, defer to `lower_expr` unchanged.
fn lower_tail_expr(
    e: &Expr,
    stmts: &mut Vec<String>,
    cx: &mut LowerCtx,
    async_tail: bool,
) -> String {
    if !async_tail {
        return lower_expr(e, stmts, cx);
    }
    match &e.kind {
        ExprKind::EffectPure(inner) => lower_expr(inner, stmts, cx),
        ExprKind::Paren(inner) => lower_tail_expr(inner, stmts, cx, true),
        ExprKind::Block(b) if b.statements.is_empty() => lower_tail_expr(&b.tail, stmts, cx, true),
        ExprKind::If {
            cond,
            then_block,
            else_block,
        } if both_simple(then_block, else_block) && !cond_has_is_bindings(cond) => {
            let cond_expr = lower_expr(cond, stmts, cx);
            let mut tstmts = Vec::new();
            let testr = lower_tail_expr(&then_block.tail, &mut tstmts, cx, true);
            debug_assert!(tstmts.is_empty());
            let mut estmts = Vec::new();
            let eestr = lower_tail_expr(&else_block.tail, &mut estmts, cx, true);
            debug_assert!(estmts.is_empty());
            format!("({cond_expr} ? {testr} : {eestr})")
        }
        _ => lower_expr(e, stmts, cx),
    }
}

fn emit_statement(out: &mut String, stmt: &Statement, cx: &mut LowerCtx, indent: usize) {
    match stmt {
        Statement::Let(l) => {
            // Track `let x = AgentName(key)` so subsequent `x.method(args)`
            // calls can dispatch through the agent class.
            if l.name.name != "_"
                && let ExprKind::Call(name, ctor_args) = &l.value.kind
                && cx.local_agents.contains(&name.name)
                && ctor_args.len() == 1
            {
                cx.local_agent_vars
                    .insert(l.name.name.clone(), name.name.clone());
            }
            let mut stmts = Vec::new();
            let value = lower_expr(&l.value, &mut stmts, cx);
            for s in &stmts {
                write_line(out, indent, s);
            }
            let bind_name = if l.name.name == "_" {
                // Emit a unique throwaway local; TS allows `const _ = ...` only
                // once per scope, so use a fresh name to be safe.
                cx.fresh()
            } else {
                l.name.name.clone()
            };
            match &l.type_annot {
                Some(annot) => write_line(
                    out,
                    indent,
                    &format!(
                        "const {bind_name}: {ty} = {value};",
                        ty = ts_type_ref(annot),
                    ),
                ),
                None => write_line(out, indent, &format!("const {bind_name} = {value};")),
            }
        }
        Statement::EffectLet(l) => {
            // `let x <- expr` → `const x = await expr;`
            let mut stmts = Vec::new();
            let value = lower_expr(&l.value, &mut stmts, cx);
            for s in &stmts {
                write_line(out, indent, s);
            }
            let bind_name = if l.name.name == "_" {
                cx.fresh()
            } else {
                l.name.name.clone()
            };
            match &l.type_annot {
                Some(annot) => write_line(
                    out,
                    indent,
                    &format!(
                        "const {bind_name}: {ty} = await {value};",
                        ty = ts_type_ref(annot),
                    ),
                ),
                None => write_line(out, indent, &format!("const {bind_name} = await {value};")),
            }
        }
        Statement::Commit(c) => {
            // Inside an agent handler, `commit expr` → `await this.commitState(expr);`
            let mut stmts = Vec::new();
            let value = lower_expr(&c.value, &mut stmts, cx);
            for s in &stmts {
                write_line(out, indent, s);
            }
            write_line(out, indent, &format!("await this.commitState({value});"));
        }
        Statement::Assert(a) => {
            // Inside a test case body, `assert expr` lowers to a runtime check
            // that throws an AssertionError so the surrounding test-case
            // runner catches it and records the failure.
            let mut stmts = Vec::new();
            let value = lower_expr(&a.value, &mut stmts, cx);
            for s in &stmts {
                write_line(out, indent, s);
            }
            let display = a.value.span.start.to_string();
            let span_start = a.value.span.start;
            let span_end = a.value.span.end;
            write_line(
                out,
                indent,
                &format!(
                    "if (!({value})) {{ throw __karnAssertionFailure(\"offset {display}\", {span_start}, {span_end}); }}",
                ),
            );
        }
    }
}

fn write_line(out: &mut String, indent: usize, line: &str) {
    for _ in 0..indent {
        out.push(' ');
    }
    out.push_str(line);
    out.push('\n');
}

/// True when a bare `Call` whose result type is the sum `sum_name` is actually a
/// variant constructor of that sum (e.g. `Won(prize)` for `Outcome`), as opposed
/// to an ordinary function that merely *returns* the sum (e.g.
/// `classify(n) -> Outcome`). Only the former is qualified to `Sum.Variant(...)`.
fn call_is_sum_variant(cx: &LowerCtx, sum_name: &str, call_name: &str) -> bool {
    if let Some(decl) = cx.commons.types.get(sum_name)
        && let TypeBody::Sum(s) = &decl.body
    {
        s.variants.iter().any(|v| v.name.name == call_name)
    } else {
        false
    }
}

/// The raw TypeScript form of a compile-time literal that v0.9.4 may admit as a
/// refined type (int or string). `None` for anything else.
fn lower_const_literal_raw(e: &Expr) -> Option<String> {
    match &e.kind {
        ExprKind::IntLit(n) => Some(n.to_string()),
        ExprKind::StrLit(s) => Some(format!("\"{}\"", escape_ts_string(s))),
        _ => None,
    }
}

fn lower_expr(e: &Expr, stmts: &mut Vec<String>, cx: &mut LowerCtx) -> String {
    // v0.9.4: a literal the checker admitted as a refined type (expected-type-
    // directed construction) is emitted through the unchecked `unsafe`
    // constructor — the refinement was already verified at compile time, so
    // there is no runtime check and no `Result`.
    if let Some(Ty::Named {
        name,
        kind: NamedKind::Refined(_),
    }) = cx.commons.expr_types.get(&e.span)
        && let Some(raw) = lower_const_literal_raw(e)
    {
        return format!("{name}.unsafe({raw})");
    }
    match &e.kind {
        ExprKind::IntLit(n) => n.to_string(),
        ExprKind::StrLit(s) => format!("\"{}\"", escape_ts_string(s)),
        ExprKind::BoolLit(b) => b.to_string(),
        ExprKind::Ident(id) => {
            // v0.9: a nullary HttpResult variant (whose checker type is
            // `HttpResult[_]`) constructs an HttpResult.<Variant>.
            if matches!(cx.commons.expr_types.get(&e.span), Some(Ty::HttpResult(_)))
                && http_variant(&id.name).is_some()
            {
                return format!("HttpResult.{}", id.name);
            }
            // A bare ident whose name matches a declared variant of a sum
            // type (and whose checker type is that sum) is a nullary
            // variant constructor reference. Qualify it as `Type.Variant`.
            // Otherwise (locals, params, `self`) emit the identifier as-is.
            if let Some(Ty::Named {
                kind: NamedKind::Sum,
                name: type_name,
            }) = cx.commons.expr_types.get(&e.span)
                && let Some(decl) = cx.commons.types.get(type_name)
                && let TypeBody::Sum(s) = &decl.body
                && s.variants.iter().any(|v| v.name.name == id.name)
            {
                return format!("{}.{}", type_name, id.name);
            }
            id.name.clone()
        }
        ExprKind::Call(name, args) => {
            // Bare variant constructor with payload → qualify.
            let args_lowered: Vec<String> = args.iter().map(|a| lower_expr(a, stmts, cx)).collect();
            // v0.9: HttpResult variant call.
            if matches!(cx.commons.expr_types.get(&e.span), Some(Ty::HttpResult(_)))
                && http_variant(&name.name).is_some()
            {
                return format!("HttpResult.{}({})", name.name, args_lowered.join(", "));
            }
            // v0.9.2: agent instantiation `AgentName(key)` lowers to the
            // generated `__makeAgentName(key)` factory, which obtains the
            // instance for this key (lookup-or-create against the registry in
            // bundle mode, or a typed DO proxy in workers mode). Skipped when
            // this Call is the receiver of a MethodCall — that path folds
            // construction and the method invocation together.
            if cx.local_agents.contains(&name.name) && args_lowered.len() == 1 {
                return cx.agent_construct(&name.name, &args_lowered[0]);
            }
            if let Some(Ty::Named {
                kind: NamedKind::Sum,
                name: type_name,
            }) = cx.commons.expr_types.get(&e.span)
                && type_name != &name.name
                && call_is_sum_variant(cx, type_name, &name.name)
            {
                return format!("{}.{}({})", type_name, name.name, args_lowered.join(", "));
            }
            format!("{}({})", name.name, args_lowered.join(", "))
        }
        ExprKind::UnaryOp(op, inner) => {
            let inner = lower_expr(inner, stmts, cx);
            let sym = match op {
                UnaryOp::Neg => "-",
                UnaryOp::Not => "!",
            };
            format!("{sym}{inner}")
        }
        ExprKind::BinOp(op, lhs, rhs) => {
            // For `&&` we need to lower `is` bindings into the rhs scope.
            // We handle that here by collecting bindings from lhs, emitting
            // them as `const` declarations before evaluating rhs — but
            // `&&` short-circuits, so simply emitting them inline is wrong.
            // We compile `lhs && (...is binding flow...) rhs` to a function
            // expression: `(lhs && ((bindings) => rhs)())`. Simpler: rely
            // on TypeScript's narrowing for the value-from-is part of
            // `is` patterns. For now, for the special pattern `x is Ok(n)`
            // we lower the rhs assuming the binding `n = x.value` was
            // captured. We use a parenthesised IIFE to scope the binding.
            if *op == BinOp::And
                && let Some((bindings, lhs_expr, rhs_expr)) = lower_and_with_is(lhs, rhs, stmts, cx)
            {
                if bindings.is_empty() {
                    return format!("{lhs_expr} && {rhs_expr}");
                }
                // Emit:  lhs && (() => { const n = ...; return rhs; })()
                let mut wrap = String::new();
                wrap.push_str(&lhs_expr);
                wrap.push_str(" && ((() => { ");
                for b in &bindings {
                    wrap.push_str(b);
                    wrap.push(' ');
                }
                wrap.push_str(&format!("return {rhs_expr}; }})())"));
                return wrap;
            }
            let l = lower_expr(lhs, stmts, cx);
            let r = lower_expr(rhs, stmts, cx);
            if *op == BinOp::Div {
                format!("Math.trunc({l} / {r})")
            } else {
                format!("{l} {} {r}", ts_binop(*op))
            }
        }
        ExprKind::Paren(inner) => {
            let s = lower_expr(inner, stmts, cx);
            format!("({s})")
        }
        ExprKind::Ok(inner) => {
            let s = lower_expr(inner, stmts, cx);
            // v0.9: `Ok` is overloaded — use the checker's recorded type to
            // decide between `Result.Ok` and `HttpResult.Ok`.
            if matches!(cx.commons.expr_types.get(&e.span), Some(Ty::HttpResult(_))) {
                format!("HttpResult.Ok({s})")
            } else {
                format!("Ok({s})")
            }
        }
        ExprKind::Err(inner) => {
            let s = lower_expr(inner, stmts, cx);
            format!("Err({s})")
        }
        ExprKind::Some(inner) => {
            let s = lower_expr(inner, stmts, cx);
            format!("Some({s})")
        }
        ExprKind::None => "None".to_string(),
        ExprKind::Question(inner) => {
            let inner_expr = lower_expr(inner, stmts, cx);
            let tmp = cx.fresh();
            stmts.push(format!("const {tmp} = {inner_expr};"));
            stmts.push(format!("if ({tmp}.tag === \"Err\") return {tmp};"));
            format!("{tmp}.value")
        }
        ExprKind::ConstructorCall {
            type_name,
            method,
            args,
        } => {
            let args: Vec<String> = args.iter().map(|a| lower_expr(a, stmts, cx)).collect();
            // Nullary variant qualified construction: `T.V` (no parens) at the
            // source level wouldn't reach here, so `T.V()` always means call.
            format!("{}.{}({})", type_name.name, method.name, args.join(", "))
        }
        ExprKind::RecordConstruction { type_name, fields } => {
            let mut parts = Vec::new();
            for f in fields {
                match &f.value {
                    Some(v) => {
                        let val = lower_expr(v, stmts, cx);
                        parts.push(format!("{}: {}", f.name.name, val));
                    }
                    None => parts.push(f.name.name.clone()),
                }
            }
            let _ = type_name;
            format!("{{ {} }}", parts.join(", "))
        }
        ExprKind::FieldAccess { receiver, field } => {
            // v0.9: `HttpResult.Variant` (nullary).
            if let ExprKind::Ident(id) = &receiver.kind
                && id.name == "HttpResult"
                && http_variant(&field.name).is_some()
            {
                return format!("HttpResult.{}", field.name);
            }
            // Agent-handler `self.state` and `self.<key>` rewrites.
            if cx.in_agent_handler
                && let ExprKind::Ident(id) = &receiver.kind
                && id.name == "self"
            {
                if field.name == "state"
                    && let Some(s) = &cx.agent_state_var
                {
                    return s.clone();
                }
                if let Some(k) = &cx.agent_key_field
                    && field.name == *k
                {
                    return format!("(this.state.id.toString() as {})", k);
                }
            }
            let r = lower_expr(receiver, stmts, cx);
            // `.raw` on an opaque value compiles to a TypeScript type
            // assertion back to the base type. The checker has already
            // verified that the receiver is opaque and the call site is
            // inside the defining commons.
            if field.name == "raw"
                && let Some(Ty::Named {
                    kind: NamedKind::Opaque(base),
                    ..
                }) = cx.commons.expr_types.get(&receiver.span)
            {
                return format!("({r} as {})", ts_base(*base));
            }
            format!("{r}.{}", field.name)
        }
        ExprKind::MethodCall {
            receiver,
            method,
            args,
        } => {
            // v0.9: explicit `HttpResult.Variant(args)` construction. The
            // checker has already recorded the expression's type — emit it
            // directly through the runtime's HttpResult namespace.
            if let ExprKind::Ident(id) = &receiver.kind
                && id.name == "HttpResult"
                && http_variant(&method.name).is_some()
            {
                let args_lowered: Vec<String> =
                    args.iter().map(|a| lower_expr(a, stmts, cx)).collect();
                return format!("HttpResult.{}({})", method.name, args_lowered.join(", "));
            }
            // v0.6 cross-context service call: receiver is an alias or the
            // dotted name of a consumed context. In bundle mode, lower to
            // `deps.surface.<key>.<method>(args as <consumed_ns>.<T>)`; in
            // workers mode (v0.8), lower to
            // `callService(deps.env.<BINDING>, "<method>", serialise...(args), deserialise_<R>)`.
            if let Some((consumed, key)) = cross_context_lowering_prefix(receiver, cx) {
                cx.cross_context_used = true;
                match cx.target {
                    BuildTarget::Bundle => {
                        let args_lowered: Vec<String> = args
                            .iter()
                            .enumerate()
                            .map(|(i, a)| {
                                let lowered = lower_expr(a, stmts, cx);
                                param_cast(&consumed, cx.cross_context, method, i, lowered)
                            })
                            .collect();
                        return format!(
                            "deps.surface.{key}.{}({})",
                            method.name,
                            args_lowered.join(", ")
                        );
                    }
                    BuildTarget::Workers => {
                        let _ = key;
                        return lower_workers_cross_context_call(
                            &consumed, method, args, stmts, cx,
                        );
                    }
                }
            }
            // Capability call: receiver is a bare ident naming a declared
            // capability in `given`. Lower to `deps.Capability.op(args)`.
            if let ExprKind::Ident(id) = &receiver.kind
                && cx.capabilities.contains(&id.name)
            {
                let args_lowered: Vec<String> =
                    args.iter().map(|a| lower_expr(a, stmts, cx)).collect();
                return format!(
                    "deps.{}.{}({})",
                    id.name,
                    method.name,
                    args_lowered.join(", ")
                );
            }
            // Static call: receiver is a bare ident naming a declared type.
            if let ExprKind::Ident(id) = &receiver.kind
                && cx.commons.types.contains_key(&id.name)
            {
                let args_lowered: Vec<String> =
                    args.iter().map(|a| lower_expr(a, stmts, cx)).collect();
                return format!("{}.{}({})", id.name, method.name, args_lowered.join(", "));
            }
            // v0.7: local service call inside a test case body.
            // `serviceName.method(args)` → `serviceName.method(args, deps)`.
            if let ExprKind::Ident(id) = &receiver.kind
                && cx.test_services.contains(&id.name)
            {
                let args_lowered: Vec<String> =
                    args.iter().map(|a| lower_expr(a, stmts, cx)).collect();
                let mut all = args_lowered;
                all.push("deps".to_string());
                return format!("{}.{}({})", id.name, method.name, all.join(", "));
            }
            // v0.9.2: inline agent invocation. Source form is
            // `Agent(<key>).method(args)`; receiver parses as
            // `Call(Agent, [<key>])`. Lower to
            // `__makeAgent(<key>).method(args, deps)`. Works in service and
            // agent-handler bodies (deps is the handler's deps parameter) and
            // test bodies (deps is the locally-built makeTestDeps record).
            if let ExprKind::Call(name, ctor_args) = &receiver.kind
                && cx.local_agents.contains(&name.name)
            {
                let key_arg = ctor_args
                    .first()
                    .map(|a| lower_expr(a, stmts, cx))
                    .unwrap_or_else(|| "\"default\"".to_string());
                let instance = cx.agent_construct(&name.name, &key_arg);
                let args_lowered: Vec<String> =
                    args.iter().map(|a| lower_expr(a, stmts, cx)).collect();
                let mut all = args_lowered;
                all.push("deps".to_string());
                return format!(
                    "{instance}.{method}({args})",
                    method = method.name,
                    args = all.join(", ")
                );
            }
            // Let-bound agent invocation. `let x = Agent(key); x.method(args)`
            // — the statement emitter recorded `x` as an agent variable when
            // it lowered the let. Method calls on `x` go straight to the
            // class instance with `deps` threaded through.
            if let ExprKind::Ident(id) = &receiver.kind
                && cx.local_agent_vars.contains_key(&id.name)
            {
                let args_lowered: Vec<String> =
                    args.iter().map(|a| lower_expr(a, stmts, cx)).collect();
                let mut all = args_lowered;
                all.push("deps".to_string());
                return format!("{}.{}({})", id.name, method.name, all.join(", "));
            }
            // Instance call: UFCS lowering with the receiver as first arg.
            let ns = cx
                .receiver_namespace(receiver)
                .unwrap_or_else(|| "/* unknown */".to_string());
            let recv = lower_expr(receiver, stmts, cx);
            let mut all = vec![recv];
            for a in args {
                all.push(lower_expr(a, stmts, cx));
            }
            format!("{ns}.{}({})", method.name, all.join(", "))
        }
        ExprKind::If {
            cond,
            then_block,
            else_block,
        } => lower_if(cond, then_block, else_block, stmts, cx),
        ExprKind::Block(b) => lower_block_as_expr(b, cx),
        ExprKind::Match { discriminant, arms } => lower_match_as_iife(discriminant, arms, cx),
        ExprKind::Is { value, pattern } => lower_is(value, pattern, stmts, cx),
        ExprKind::UnitLit => "undefined".to_string(),
        ExprKind::EffectPure(inner) => {
            let inner_expr = lower_expr(inner, stmts, cx);
            format!("Promise.resolve({inner_expr})")
        }
        ExprKind::RecordSpread {
            type_name: _,
            base,
            overrides,
        } => {
            let base_expr = lower_expr(base, stmts, cx);
            let mut parts = vec![format!("...{base_expr}")];
            for f in overrides {
                match &f.value {
                    Some(v) => {
                        let val = lower_expr(v, stmts, cx);
                        parts.push(format!("{}: {}", f.name.name, val));
                    }
                    None => parts.push(f.name.name.clone()),
                }
            }
            format!("{{ {} }}", parts.join(", "))
        }
        ExprKind::Assert(inner) => {
            // v0.9.1: assert as an expression. Emit a runtime helper call
            // that returns void (i.e., evaluates to `undefined` at runtime
            // and is treated as the unit value `()` in Karn terms).
            let value = lower_expr(inner, stmts, cx);
            let display = inner.span.start;
            let span_start = inner.span.start;
            let span_end = inner.span.end;
            format!("__karnAssert(({value}), \"offset {display}\", {span_start}, {span_end})")
        }
        ExprKind::Mock { type_ref, args } => lower_mock(type_ref, args, stmts, cx),
    }
}

/// A default base-type literal (as TypeScript source) that satisfies a refined
/// type's predicates, for bare `Mock[T]`. `None` when no default can be derived
/// (a `Matches` refinement — the checker rejects bare `Mock` for those).
fn refined_default(decl: &TypeDecl) -> Option<String> {
    let (base, refinement) = match &decl.body {
        TypeBody::Refined {
            base, refinement, ..
        } => (*base, refinement.as_ref()),
        _ => return None,
    };
    match base {
        BaseType::Int => {
            let mut lo: i64 = 0;
            if let Some(r) = refinement {
                for p in &r.predicates {
                    match p.kind {
                        PredKind::Positive => lo = lo.max(1),
                        PredKind::NonNegative => lo = lo.max(0),
                        PredKind::InRange(a, _) => lo = lo.max(a),
                        _ => {}
                    }
                }
            }
            Some(lo.to_string())
        }
        BaseType::String => {
            let mut len: i64 = 0;
            if let Some(r) = refinement {
                for p in &r.predicates {
                    match p.kind {
                        PredKind::NonEmpty => len = len.max(1),
                        PredKind::MinLength(k) | PredKind::Length(k) => len = len.max(k),
                        PredKind::Matches(_) => return None,
                        _ => {}
                    }
                }
            }
            if len < 1 {
                len = 1;
            }
            Some(format!("\"{}\"", "x".repeat(len as usize)))
        }
        BaseType::Bool => Some("true".to_string()),
    }
}

/// v0.9.4 Part B (slice 1): lower a refined-type `Mock[T]` / `Mock[T](lit)` to
/// the branded `unsafe` constructor. The checker has already validated this is a
/// refined type in a test body, and recorded the refined type at `span`.
/// v0.9.4 slice 2 recursion cap for bare `Mock` generation (mirrors the
/// checker's `MOCK_DEPTH`).
const MOCK_DEPTH: u32 = 12;

/// A TypeScript base-literal default for an opaque type's underlying base. Not
/// distinct per call in this increment — per-call distinctness via a runtime
/// counter is a follow-up.
fn base_default_ts(base: BaseType) -> String {
    match base {
        BaseType::Int => "0".to_string(),
        BaseType::String => "\"mock\"".to_string(),
        BaseType::Bool => "true".to_string(),
    }
}

/// Generate a TypeScript expression for a bare `Mock` of `ty` (v0.9.4 Part B,
/// slice 2). Recurses through sum payloads and record fields; refined types use
/// `refined_default`, opaque types wrap a base default, bare bases use 0/""/true.
fn mock_value(ty: &Ty, cx: &LowerCtx, depth: u32) -> String {
    if depth == 0 {
        return "undefined".to_string();
    }
    match ty {
        Ty::Base(BaseType::Int) => "0".to_string(),
        Ty::Base(BaseType::String) => "\"\"".to_string(),
        Ty::Base(BaseType::Bool) => "true".to_string(),
        Ty::Named { name, .. } => {
            let Some(decl) = cx.commons.types.get(name) else {
                return "undefined".to_string();
            };
            match &decl.body {
                TypeBody::Refined { .. } => {
                    let d = refined_default(decl).unwrap_or_else(|| "0".to_string());
                    format!("{name}.unsafe({d})")
                }
                TypeBody::Opaque { base, .. } => {
                    format!("{name}.unsafe({})", base_default_ts(*base))
                }
                TypeBody::Sum(s) => match s.variants.first() {
                    None => "undefined".to_string(),
                    Some(v) if v.payload.is_empty() => format!("{name}.{}", v.name.name),
                    Some(v) => {
                        let args: Vec<String> = v
                            .payload
                            .iter()
                            .map(|f| {
                                crate::checker::resolve_type_ref(&f.type_ref, &cx.commons.types)
                                    .map(|t| mock_value(&t, cx, depth - 1))
                                    .unwrap_or_else(|| "undefined".to_string())
                            })
                            .collect();
                        format!("{name}.{}({})", v.name.name, args.join(", "))
                    }
                },
                TypeBody::Record(r) => {
                    let parts: Vec<String> = r
                        .fields
                        .iter()
                        .map(|f| {
                            let fv =
                                crate::checker::resolve_type_ref(&f.type_ref, &cx.commons.types)
                                    .map(|t| mock_value(&t, cx, depth - 1))
                                    .unwrap_or_else(|| "undefined".to_string());
                            format!("{}: {}", f.name.name, fv)
                        })
                        .collect();
                    format!("{{ {} }}", parts.join(", "))
                }
            }
        }
        _ => "undefined".to_string(),
    }
}

fn lower_mock(
    type_ref: &TypeRef,
    args: &[Expr],
    stmts: &mut Vec<String>,
    cx: &mut LowerCtx,
) -> String {
    // Resolve the mocked type straight from the AST node rather than the
    // checker's `expr_types` side-table — the static type table is always
    // populated, whereas a test body's per-expression types may not be visible
    // to the emitter.
    let ty = match crate::checker::resolve_type_ref(type_ref, &cx.commons.types) {
        Some(t) => t,
        None => return "undefined /* mock: unresolved type */".to_string(),
    };
    // Refined literal pin → `T.unsafe(<literal>)`.
    if let (
        Some(arg),
        Ty::Named {
            name,
            kind: NamedKind::Refined(_),
        },
    ) = (args.first(), &ty)
    {
        let raw = lower_const_literal_raw(arg).unwrap_or_else(|| lower_expr(arg, stmts, cx));
        return format!("{name}.unsafe({raw})");
    }
    // Bare mock (refined / opaque / sum / record).
    mock_value(&ty, cx, MOCK_DEPTH)
}

/// When we encounter `lhs && rhs`, see if lhs is an `is` (possibly wrapped
/// in parens or nested `&&`) and if so collect the bindings to inject into
/// rhs. Returns `(binding_const_decls, lowered_lhs, lowered_rhs)` if
/// special handling is appropriate; otherwise returns None.
fn lower_and_with_is(
    lhs: &Expr,
    rhs: &Expr,
    stmts: &mut Vec<String>,
    cx: &mut LowerCtx,
) -> Option<(Vec<String>, String, String)> {
    // Probe structurally (no lowering) so a `&&` without an `is` falls through
    // to the caller's ordinary lowering untouched. This mirrors exactly the
    // shapes `gather_is_bindings_for_emit` walks (`&&` and parens), preserving
    // the original "Some iff lhs contains an `is`" behaviour.
    if !cond_contains_is(lhs) {
        return None;
    }
    // Lower lhs *before* gathering its bindings. Lowering lifts any complex
    // `is` receiver (e.g. a call) into a shared temp recorded on `cx`; the
    // binding gatherer below then references that temp via `is_receiver_text`
    // instead of re-emitting the receiver. For simple receivers nothing is
    // cached and the output is byte-identical to before.
    let lhs_expr = lower_expr(lhs, stmts, cx);
    let mut bindings = Vec::new();
    let mut found = false;
    gather_is_bindings_for_emit(lhs, cx, &mut bindings, &mut found);
    let _ = found; // guaranteed true by the `cond_contains_is` guard above
    // We lower rhs ourselves (not into the outer stmts), so that any
    // statement-style prefix from rhs is folded into the IIFE properly.
    let mut rhs_stmts = Vec::new();
    let rhs_expr = lower_expr(rhs, &mut rhs_stmts, cx);
    let mut rhs_full = String::new();
    for s in &rhs_stmts {
        rhs_full.push_str(s);
        rhs_full.push(' ');
    }
    rhs_full.push_str(&rhs_expr);
    Some((bindings, lhs_expr, rhs_full))
}

/// Walk an expression collecting `const name = expr.field;` strings for
/// any `is`-pattern bindings on the truthy path. `found` indicates whether
/// at least one `is` was seen.
fn gather_is_bindings_for_emit(e: &Expr, cx: &LowerCtx, out: &mut Vec<String>, found: &mut bool) {
    match &e.kind {
        ExprKind::Is { value, pattern } => {
            *found = true;
            let value_text = cx.is_receiver_text(value);
            let disc_ty = cx.commons.expr_types.get(&value.span).cloned();
            if let Pattern::Variant {
                variant, bindings, ..
            } = pattern
            {
                for (i, b) in bindings.iter().enumerate() {
                    if b.is_wildcard() {
                        continue;
                    }
                    match &b.kind {
                        PatternBindingKind::Named { field, name } => {
                            out.push(format!(
                                "const {name} = {value}.{field};",
                                name = name.name,
                                value = value_text,
                                field = field.name
                            ));
                        }
                        PatternBindingKind::Positional { name } => {
                            let field =
                                cx.positional_field_name(disc_ty.as_ref(), &variant.name, i);
                            out.push(format!(
                                "const {name} = {value}.{field};",
                                name = name.name,
                                value = value_text,
                                field = field
                            ));
                        }
                    }
                }
            }
        }
        ExprKind::BinOp(BinOp::And, l, r) => {
            gather_is_bindings_for_emit(l, cx, out, found);
            gather_is_bindings_for_emit(r, cx, out, found);
        }
        ExprKind::Paren(inner) => gather_is_bindings_for_emit(inner, cx, out, found),
        _ => {}
    }
}

/// True when an `is` receiver is a simple, side-effect-free, repeatable
/// lvalue — an identifier or a field-access chain ending at one (optionally
/// parenthesised). Such receivers can be referenced textually as many times
/// as there are pattern bindings without re-evaluation. Anything else (calls,
/// matches, arithmetic, …) must be lifted to a temp before use; see
/// `LowerCtx::is_receiver_ref`.
fn is_simple_is_receiver(value: &Expr) -> bool {
    match &value.kind {
        ExprKind::Ident(_) => true,
        ExprKind::FieldAccess { receiver, .. } => is_simple_is_receiver(receiver),
        ExprKind::Paren(inner) => is_simple_is_receiver(inner),
        _ => false,
    }
}

/// Render a *simple* `is` receiver (see `is_simple_is_receiver`) as a textual
/// reference for binding lookups. Complex receivers never reach this function
/// — they are lifted to a temp and resolved via the span cache in
/// `LowerCtx::is_receiver_text` — so the final arm is a defensive backstop the
/// `no_unknown_placeholder_in_emitted_output` test also guards against.
fn value_text_for_is(value: &Expr) -> String {
    match &value.kind {
        ExprKind::Ident(id) => id.name.clone(),
        ExprKind::FieldAccess { receiver, field } => {
            format!("{}.{}", value_text_for_is(receiver), field.name)
        }
        ExprKind::Paren(inner) => value_text_for_is(inner),
        _ => "(/* TODO: complex is-receiver */ )".to_string(),
    }
}

fn lower_if(
    cond: &Expr,
    then_block: &Block,
    else_block: &Block,
    stmts: &mut Vec<String>,
    cx: &mut LowerCtx,
) -> String {
    let cond_expr = lower_expr(cond, stmts, cx);
    // If the cond contains `is`-bindings, the then-branch needs a place
    // for the `const name = receiver.field;` declarations — a ternary
    // has no such place. Force the IIFE form.
    if both_simple(then_block, else_block) && !cond_has_is_bindings(cond) {
        let mut tstmts = Vec::new();
        let testr = lower_expr(&then_block.tail, &mut tstmts, cx);
        debug_assert!(tstmts.is_empty());
        let mut estmts = Vec::new();
        let eestr = lower_expr(&else_block.tail, &mut estmts, cx);
        debug_assert!(estmts.is_empty());
        format!("({cond_expr} ? {testr} : {eestr})")
    } else {
        let mut iife = String::new();
        iife.push_str("(() => {\n");
        iife.push_str("    if (");
        iife.push_str(&cond_expr);
        iife.push_str(") {\n");
        // Inject is-binding declarations on the truthy side.
        let mut is_bindings = Vec::new();
        let mut found = false;
        gather_is_bindings_for_emit(cond, cx, &mut is_bindings, &mut found);
        for b in &is_bindings {
            for _ in 0..(INDENT_STEP * 3) {
                iife.push(' ');
            }
            iife.push_str(b);
            iife.push('\n');
        }
        emit_block_as_function_body(&mut iife, then_block, cx, INDENT_STEP * 3, false);
        for _ in 0..(INDENT_STEP * 2) {
            iife.push(' ');
        }
        iife.push_str("} else {\n");
        emit_block_as_function_body(&mut iife, else_block, cx, INDENT_STEP * 3, false);
        for _ in 0..(INDENT_STEP * 2) {
            iife.push(' ');
        }
        iife.push_str("}\n");
        for _ in 0..INDENT_STEP {
            iife.push(' ');
        }
        iife.push_str("})()");
        iife
    }
}

/// True if `e` contains an `is` test reachable through `&&` and parentheses —
/// matching exactly the shapes `gather_is_bindings_for_emit` walks (note: it
/// does *not* descend into `||`). Used by `lower_and_with_is` to decide whether
/// the `is`-binding flow applies before doing any lowering.
fn cond_contains_is(e: &Expr) -> bool {
    match &e.kind {
        ExprKind::Is { .. } => true,
        ExprKind::BinOp(BinOp::And, l, r) => cond_contains_is(l) || cond_contains_is(r),
        ExprKind::Paren(inner) => cond_contains_is(inner),
        _ => false,
    }
}

/// True if the expression contains an `is` test with at least one
/// non-wildcard binding. Walks through `&&`, `||`, and parens.
fn cond_has_is_bindings(e: &Expr) -> bool {
    match &e.kind {
        ExprKind::Is {
            pattern: Pattern::Variant { bindings, .. },
            ..
        } => bindings.iter().any(|b| !b.is_wildcard()),
        ExprKind::BinOp(BinOp::And, l, r) | ExprKind::BinOp(BinOp::Or, l, r) => {
            cond_has_is_bindings(l) || cond_has_is_bindings(r)
        }
        ExprKind::Paren(inner) => cond_has_is_bindings(inner),
        _ => false,
    }
}

fn emit_if_tail(
    out: &mut String,
    cond: &Expr,
    then_block: &Block,
    else_block: &Block,
    cx: &mut LowerCtx,
    indent: usize,
    async_tail: bool,
) {
    let mut pre = Vec::new();
    let cond_expr = lower_expr(cond, &mut pre, cx);
    for s in &pre {
        write_line(out, indent, s);
    }
    write_line(out, indent, &format!("if ({cond_expr}) {{"));
    // is-binding declarations on the truthy path.
    let mut is_bindings = Vec::new();
    let mut found = false;
    gather_is_bindings_for_emit(cond, cx, &mut is_bindings, &mut found);
    for b in &is_bindings {
        write_line(out, indent + INDENT_STEP, b);
    }
    emit_block_as_function_body(out, then_block, cx, indent + INDENT_STEP, async_tail);
    write_line(out, indent, "} else {");
    emit_block_as_function_body(out, else_block, cx, indent + INDENT_STEP, async_tail);
    write_line(out, indent, "}");
}

fn both_simple(a: &Block, b: &Block) -> bool {
    a.statements.is_empty()
        && b.statements.is_empty()
        && simple_expr(&a.tail)
        && simple_expr(&b.tail)
}

fn simple_expr(e: &Expr) -> bool {
    match &e.kind {
        ExprKind::Question(_) => false,
        ExprKind::Match { .. } => false,
        ExprKind::Block(b) => b.statements.is_empty() && simple_expr(&b.tail),
        ExprKind::If {
            then_block,
            else_block,
            cond,
        } => simple_expr(cond) && both_simple(then_block, else_block),
        ExprKind::Ok(i) | ExprKind::Err(i) | ExprKind::Some(i) => simple_expr(i),
        ExprKind::Paren(i) | ExprKind::UnaryOp(_, i) => simple_expr(i),
        ExprKind::BinOp(_, l, r) => simple_expr(l) && simple_expr(r),
        ExprKind::Call(_, args) | ExprKind::ConstructorCall { args, .. } => {
            args.iter().all(simple_expr)
        }
        ExprKind::MethodCall { receiver, args, .. } => {
            simple_expr(receiver) && args.iter().all(simple_expr)
        }
        ExprKind::FieldAccess { receiver, .. } => simple_expr(receiver),
        ExprKind::RecordConstruction { fields, .. } => fields.iter().all(|f| match &f.value {
            Some(v) => simple_expr(v),
            None => true,
        }),
        ExprKind::Is { value, .. } => simple_expr(value),
        _ => true,
    }
}

fn lower_block_as_expr(b: &Block, cx: &mut LowerCtx) -> String {
    let mut iife = String::new();
    iife.push_str("(() => {\n");
    // IIFE is a synchronous arrow function; the surrounding expression context
    // expects a concrete value, so `Effect.pure(...)` must still wrap as
    // `Promise.resolve(...)`.
    emit_block_as_function_body(&mut iife, b, cx, INDENT_STEP * 2, false);
    for _ in 0..INDENT_STEP {
        iife.push(' ');
    }
    iife.push_str("})()");
    iife
}

fn lower_match_as_iife(discriminant: &Expr, arms: &[MatchArm], cx: &mut LowerCtx) -> String {
    let disc_ty = cx.commons.expr_types.get(&discriminant.span).cloned();
    let mut stmts = Vec::new();
    let disc = lower_expr(discriminant, &mut stmts, cx);
    let mut iife = String::new();
    // Pre-statements need to be evaluated before the IIFE; lift them into
    // a sequence: `(prestmt1, prestmt2, iife)`. Since TS doesn't let us
    // evaluate statements inline, we wrap in another arrow.
    let inner_iife = maybe_async_iife(build_match_iife(&disc, &disc_ty, arms, cx));
    if stmts.is_empty() {
        iife.push_str(&inner_iife);
    } else {
        iife.push_str("(() => {\n");
        for s in &stmts {
            for _ in 0..(INDENT_STEP * 2) {
                iife.push(' ');
            }
            iife.push_str(s);
            iife.push('\n');
        }
        for _ in 0..(INDENT_STEP * 2) {
            iife.push(' ');
        }
        iife.push_str("return ");
        iife.push_str(&inner_iife);
        iife.push_str(";\n");
        for _ in 0..INDENT_STEP {
            iife.push(' ');
        }
        iife.push_str("})()");
        iife = maybe_async_iife(iife);
    }
    iife
}

/// v0.9.2: a match lowered to an IIFE in *expression* position may have arms
/// that `await` (an effectful `let x <- …` or an effectful tail). A synchronous
/// arrow can't host `await`, so when the lowered body contains one, make the
/// outermost arrow `async` and `await` its call. Nested matches that already
/// did this surface their own `await`, which propagates the transform outward.
fn maybe_async_iife(iife: String) -> String {
    if !iife.contains("await ") {
        return iife;
    }
    let async_iife = if let Some(rest) = iife.strip_prefix("((__d) =>") {
        format!("(async (__d) =>{rest}")
    } else if let Some(rest) = iife.strip_prefix("(() => {") {
        format!("(async () => {{{rest}")
    } else {
        return iife;
    };
    format!("await {async_iife}")
}

fn build_match_iife(
    disc_expr: &str,
    disc_ty: &Option<Ty>,
    arms: &[MatchArm],
    cx: &mut LowerCtx,
) -> String {
    let mut out = String::new();
    out.push_str("((__d) => {\n");
    for _ in 0..(INDENT_STEP * 2) {
        out.push(' ');
    }
    out.push_str("switch (__d.tag) {\n");
    for arm in arms {
        // IIFE form (non-tail match expression): `Effect.pure(...)` must keep
        // its `Promise.resolve` wrapper because the IIFE is a synchronous arrow.
        emit_match_case(&mut out, "__d", disc_ty, arm, cx, INDENT_STEP * 3, false);
    }
    for _ in 0..(INDENT_STEP * 2) {
        out.push(' ');
    }
    out.push_str("}\n");
    for _ in 0..(INDENT_STEP * 2) {
        out.push(' ');
    }
    out.push_str("throw new Error(\"non-exhaustive match\");\n");
    for _ in 0..INDENT_STEP {
        out.push(' ');
    }
    out.push_str(&format!("}})({disc_expr})"));
    out
}

/// Whether an expression lowers to a stable reference TypeScript can narrow
/// across a `switch` (a variable or a property path rooted in one). Calls and
/// other computed expressions are not narrowable and must be bound to a temp.
fn is_narrowable_path(e: &Expr) -> bool {
    match &e.kind {
        ExprKind::Ident(_) => true,
        ExprKind::FieldAccess { receiver, .. } => is_narrowable_path(receiver),
        ExprKind::Paren(inner) => is_narrowable_path(inner),
        _ => false,
    }
}

fn emit_match_tail(
    out: &mut String,
    discriminant: &Expr,
    arms: &[MatchArm],
    cx: &mut LowerCtx,
    indent: usize,
    async_tail: bool,
) {
    let mut pre = Vec::new();
    let mut disc = lower_expr(discriminant, &mut pre, cx);
    let disc_ty = cx.commons.expr_types.get(&discriminant.span).cloned();
    for s in &pre {
        write_line(out, indent, s);
    }
    // v0.9.2: a statement-position `switch` narrows the scrutinee only when it
    // is a stable reference (a variable or property path). A call discriminant
    // such as `ShortCode.of(raw)` is re-evaluated per arm and TypeScript cannot
    // narrow it (and re-evaluation could repeat side effects), so bind it to a
    // fresh temp once and switch on that.
    if !is_narrowable_path(discriminant) {
        let tmp = cx.fresh();
        write_line(out, indent, &format!("const {tmp} = {disc};"));
        disc = tmp;
    }
    write_line(out, indent, &format!("switch ({disc}.tag) {{"));
    for arm in arms {
        emit_match_case(
            out,
            &disc,
            &disc_ty,
            arm,
            cx,
            indent + INDENT_STEP,
            async_tail,
        );
    }
    write_line(out, indent, "}");
    write_line(out, indent, "throw new Error(\"non-exhaustive match\");");
}

fn emit_match_case(
    out: &mut String,
    disc_var: &str,
    disc_ty: &Option<Ty>,
    arm: &MatchArm,
    cx: &mut LowerCtx,
    indent: usize,
    async_tail: bool,
) {
    match &arm.pattern {
        Pattern::Wildcard(_) => {
            write_line(out, indent, "default: {");
            emit_match_body(out, &arm.body, cx, indent + INDENT_STEP, async_tail);
            write_line(out, indent, "}");
        }
        Pattern::Variant {
            variant, bindings, ..
        } => {
            write_line(
                out,
                indent,
                &format!("case \"{tag}\": {{", tag = variant.name),
            );
            for (i, b) in bindings.iter().enumerate() {
                if b.is_wildcard() {
                    continue;
                }
                let field = match &b.kind {
                    PatternBindingKind::Named { field, .. } => field.name.clone(),
                    PatternBindingKind::Positional { .. } => {
                        cx.positional_field_name(disc_ty.as_ref(), &variant.name, i)
                    }
                };
                let local = b.local_name().name.clone();
                write_line(
                    out,
                    indent + INDENT_STEP,
                    &format!("const {local} = {disc_var}.{field};"),
                );
            }
            emit_match_body(out, &arm.body, cx, indent + INDENT_STEP, async_tail);
            write_line(out, indent, "}");
        }
    }
}

fn emit_match_body(
    out: &mut String,
    body: &MatchBody,
    cx: &mut LowerCtx,
    indent: usize,
    async_tail: bool,
) {
    match body {
        MatchBody::Expr(e) => {
            let mut stmts = Vec::new();
            let v = lower_tail_expr(e, &mut stmts, cx, async_tail);
            for s in &stmts {
                write_line(out, indent, s);
            }
            write_line(out, indent, &format!("return {v};"));
        }
        MatchBody::Block(b) => emit_block_as_function_body(out, b, cx, indent, async_tail),
    }
}

fn lower_is(value: &Expr, pattern: &Pattern, stmts: &mut Vec<String>, cx: &mut LowerCtx) -> String {
    let v = cx.is_receiver_ref(value, stmts);
    match pattern {
        Pattern::Wildcard(_) => "true".to_string(),
        Pattern::Variant { variant, .. } => {
            format!("{v}.tag === \"{}\"", variant.name)
        }
    }
}

fn ts_base(b: BaseType) -> &'static str {
    match b {
        BaseType::Int => "number",
        BaseType::String => "string",
        BaseType::Bool => "boolean",
    }
}

pub(crate) fn ts_type_ref(r: &TypeRef) -> String {
    match r {
        TypeRef::Base(b, _) => ts_base(*b).to_string(),
        TypeRef::Named(id) => id.name.clone(),
        TypeRef::Result(t, e, _) => format!("Result<{}, {}>", ts_type_ref(t), ts_type_ref(e)),
        TypeRef::Option(t, _) => format!("Option<{}>", ts_type_ref(t)),
        TypeRef::Effect(t, _) => {
            let inner = ts_type_ref(t);
            if inner == "()" || inner == "void" {
                "Promise<void>".to_string()
            } else {
                format!("Promise<{inner}>")
            }
        }
        TypeRef::HttpResult(t, _) => format!("HttpResult<{}>", ts_type_ref(t)),
        TypeRef::ValidationError(_) => "ValidationError".to_string(),
        TypeRef::Unit(_) => "void".to_string(),
    }
}

fn ts_binop(op: BinOp) -> &'static str {
    match op {
        BinOp::Or => "||",
        BinOp::And => "&&",
        BinOp::Eq => "===",
        BinOp::NotEq => "!==",
        BinOp::Lt => "<",
        BinOp::LtEq => "<=",
        BinOp::Gt => ">",
        BinOp::GtEq => ">=",
        BinOp::Add => "+",
        BinOp::Sub => "-",
        BinOp::Mul => "*",
        BinOp::Div => "/",
    }
}

fn escape_ts_string(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for c in s.chars() {
        match c {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\t' => out.push_str("\\t"),
            '\r' => out.push_str("\\r"),
            c => out.push(c),
        }
    }
    out
}

