// @ts-check
//! Remark plugin: expand the mdBook authoring directives the Bynk Book uses, so
//! the same source renders under Astro/Starlight (documentation track, slice 2).
//!
//! Handled (matching the mdBook preprocessors `mdbook-bynk-grammar` and
//! `mdbook-bynk-visuals`):
//!   - `{{#grammar <rule>}}`           → an ```ebnf code block from the committed
//!                                        grammar render (`generated/grammar.json`).
//!   - `{{#grammar-semantics <rule>}}` → the diagnostics bullet list for a rule
//!                                        from `generated/grammar-semantics.json`
//!                                        (or the "no diagnostics" line).
//!   - `{{#include <path>}}`           → inline a fixture's contents inside the
//!                                        code fence it sits in (diagnostics pairs).
//!   - `[!NOTE|TIP|WARNING|DANGER]`    → a Starlight aside (note/tip/caution/danger).
//!
//! The grammar/semantics JSON is generated by Rust drift guards
//! (`bynk-grammar`/`bynkc`), so the rendered grammar can never drift from the
//! mdBook output or the compiler's diagnostics registry.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { visit } from "unist-util-visit";

const GENERATED = fileURLToPath(new URL("../generated/", import.meta.url));
const GRAMMAR = JSON.parse(fs.readFileSync(path.join(GENERATED, "grammar.json"), "utf8"));
const SEMANTICS = JSON.parse(
  fs.readFileSync(path.join(GENERATED, "grammar-semantics.json"), "utf8"),
);

/** mdBook `[!KIND]` callout → Starlight aside variant (track §5.3). */
const ASIDE_KIND = { NOTE: "note", TIP: "tip", WARNING: "caution", DANGER: "danger" };

const GRAMMAR_DIRECTIVE = /^\{\{#(grammar|grammar-semantics)\s+(\S+)\}\}$/;
const INCLUDE = /\{\{#include\s+(\S+)\}\}/g;
const ALERT = /^\[!(NOTE|TIP|WARNING|DANGER)\]\s*\n?/;

/**
 * @param {{ includeBase: string }} options
 *   `includeBase` is the directory that `{{#include}}` paths' `diagnostics/…`
 *   suffix resolves against (the repo's `docs/` today; repointed when the
 *   diagnostics fixtures relocate).
 */
export default function remarkBynkDirectives(options) {
  const includeBase = options?.includeBase;
  if (!includeBase) throw new Error("remark-bynk-directives: `includeBase` is required");

  return (tree, file) => {
    const where = file?.path ?? "<unknown>";

    // mdBook annotates fences with a comma list (```bynk,fail) — Expressive Code
    // reads the whole token as the language. Keep only the language so the block
    // still highlights; the failing-example flags are conveyed by the prose and
    // the paired error transcript.
    visit(tree, "code", (node) => {
      if (node.lang && node.lang.includes(",")) {
        node.lang = node.lang.split(",")[0] || null;
      }
      // Shiki has no EBNF grammar, so a literal ```ebnf fence (the generated
      // grammar-appendix pages) renders as a plain frame — same as the
      // {{#grammar}} output — instead of warning that the language is missing.
      if (node.lang === "ebnf") node.lang = null;
    });

    // ```mermaid fences → a real `<pre class="mermaid">` hast element that
    // rehype-mermaid renders to inline SVG at build. Emitting a non-`code` node
    // with `hName`/`hChildren` (rather than a raw HTML string, which Astro leaves
    // unparsed) both keeps Starlight's Expressive Code from claiming the fence
    // and gives rehype-mermaid the genuine element it looks for.
    visit(tree, "code", (node, index, parent) => {
      if (node.lang !== "mermaid" || parent == null || index == null) return;
      parent.children[index] = {
        type: "mermaid",
        data: {
          hName: "pre",
          hProperties: { className: ["mermaid"] },
          hChildren: [{ type: "text", value: node.value }],
        },
      };
    });

    // {{#include}} lives inside ```bynk,fail / ```text fences — expand in-place.
    visit(tree, "code", (node) => {
      if (!node.value.includes("{{#include")) return;
      node.value = node.value.replace(INCLUDE, (_match, rel) =>
        readInclude(rel, includeBase, where),
      );
    });

    // {{#grammar}} / {{#grammar-semantics}} sit on their own line → a paragraph
    // whose only child is the directive text.
    visit(tree, "paragraph", (node, index, parent) => {
      if (parent == null || index == null) return;
      if (node.children.length !== 1 || node.children[0].type !== "text") return;
      const match = GRAMMAR_DIRECTIVE.exec(node.children[0].value.trim());
      if (!match) return;
      const [, kind, name] = match;
      const replacement =
        kind === "grammar" ? grammarBlock(name, where) : semanticsNodes(name);
      parent.children.splice(index, 1, ...replacement);
      return index + replacement.length; // continue past the inserted nodes
    });

    // GitHub-style alert blockquotes → Starlight asides.
    visit(tree, "blockquote", (node, index, parent) => {
      if (parent == null || index == null) return;
      const aside = alertToAside(node);
      if (aside) parent.children.splice(index, 1, aside);
    });

    // mdBook custom heading ids: `## Heading {#custom-id}` → a heading whose
    // rendered id is `custom-id` (so the Book's intra-page anchors resolve).
    visit(tree, "heading", (node) => applyHeadingId(node));
  };
}

/** `{{#grammar <rule>}}` → one ```ebnf code block. Unknown rule fails the build. */
function grammarBlock(name, where) {
  const production = GRAMMAR.productions[name];
  if (production == null) {
    throw new Error(
      `${where}: {{#grammar ${name}}} — no such grammar rule in generated/grammar.json`,
    );
  }
  // No `lang`: Shiki has no EBNF grammar, so a plain code frame is the honest
  // render (and avoids a per-block "language not found" build warning).
  return [{ type: "code", lang: null, value: production }];
}

/** `{{#grammar-semantics <rule>}}` → the diagnostics bullet list (or the empty line). */
function semanticsNodes(name) {
  const diagnostics = Array.isArray(SEMANTICS[name]) ? SEMANTICS[name] : [];
  if (diagnostics.length === 0) {
    return [
      {
        type: "paragraph",
        children: [
          { type: "emphasis", children: [text("No diagnostics constrain this construct directly.")] },
        ],
      },
    ];
  }
  const items = diagnostics.map((diagnostic) => ({
    type: "listItem",
    spread: false,
    children: [
      {
        type: "paragraph",
        children: [
          { type: "inlineCode", value: diagnostic.code },
          text(` — ${diagnostic.summary}`),
        ],
      },
    ],
  }));
  return [
    { type: "list", ordered: false, spread: false, children: items },
    {
      type: "paragraph",
      children: [
        text("See the "),
        {
          type: "link",
          url: "/book/reference/diagnostics/",
          children: [text("diagnostic index")],
        },
        text(" for all codes."),
      ],
    },
  ];
}

/** Resolve a `{{#include}}` path against the diagnostics base and inline it. */
function readInclude(rel, includeBase, where) {
  const anchor = rel.indexOf("diagnostics/");
  if (anchor === -1) {
    throw new Error(`${where}: {{#include ${rel}}} — only diagnostics/* fixtures are supported`);
  }
  const abs = path.join(includeBase, rel.slice(anchor));
  let body;
  try {
    body = fs.readFileSync(abs, "utf8");
  } catch {
    throw new Error(`${where}: {{#include ${rel}}} — cannot read ${abs}`);
  }
  return body.replace(/\n$/, ""); // drop the trailing newline; the fence adds one
}

/** A GitHub-alert blockquote → a Starlight aside (containerDirective). */
function alertToAside(node) {
  const firstText = findFirstText(node);
  if (!firstText) return null;
  const match = ALERT.exec(firstText.value);
  if (!match) return null;
  firstText.value = firstText.value.replace(ALERT, "");
  // If the marker was the whole first line, an empty leading text node may remain.
  return {
    type: "containerDirective",
    name: ASIDE_KIND[match[1]],
    attributes: {},
    children: node.children,
  };
}

/** Honor a trailing `{#custom-id}` on a heading by pinning the rendered id. */
function applyHeadingId(node) {
  const last = node.children[node.children.length - 1];
  if (!last || last.type !== "text") return;
  const match = /\s*\{#([\w-]+)\}\s*$/.exec(last.value);
  if (!match) return;
  last.value = last.value.slice(0, match.index);
  if (last.value === "") node.children.pop();
  node.data ??= {};
  node.data.id = match[1];
  node.data.hProperties = { ...(node.data.hProperties ?? {}), id: match[1] };
}

/** The first descendant `text` node, depth-first. */
function findFirstText(node) {
  if (node.type === "text") return node;
  if (!Array.isArray(node.children)) return null;
  for (const child of node.children) {
    const found = findFirstText(child);
    if (found) return found;
  }
  return null;
}

function text(value) {
  return { type: "text", value };
}
